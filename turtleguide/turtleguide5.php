<h2>Turtlekaze</h2>
<p>
This technique allows you to get maximum damage out of shot 2, 
but the tradeoff is that you also damage yourself. It's sort of like using 
the blood powerup, but stronger. To do a turtlekaze you must walk
Turtle so that it is almost completely on top of the enemy, use your 
lowest angle, and tap shot 2. I think a little power is better than 0 power, 
and I'd avoid using more than a little.
<p>
<img src="okkaze.jpg">
<p>
It's best if 
you can be angled downward a bit, or if the enemy is on a hill, so that 
you're sort of facing their belly. But it's possible even if you can't 
get that position.
<p>
<img src="goodkaze.jpg">
<p>
When you're positioned properly, the shot will start to emerge from 
Turtle's center (not from his cannon), then immediately explode when it hits the enemy's 
core. The goal of turtlekaze is to get your shot to hit very deep into the 
enemy's core, deeper than a normal shotgun or high angle shot can 
penetrate. You don't want to just get Turtle's red aimer or the blue tip of his 
cannon the middle. You need to almost be on top of them. If you're not, 
you don't get the nice damage and you STILL hurt yourself. 
<p>
<img src="badkaze.jpg">
<p>
When done correctly the damage is huge, 350-400 sometimes. But, Turtle 
will take at least 200 damage too. So usually this isn't a 
good tradeoff. There are some situations where it's a smart to use it - 
for example, at the beginning of the match, let's say the 
enemy opens with an SS. You may not have the angle for an SS (but you 
can do turtlekaze). The best you can do is beat their delay with a shot 2.
If you do your shot 2 deep in their body, then do a dual shot 2 on your 
next turn from the same position, you can kill them before they get to 
shoot again. If didn't turtlekaze and they lived, they would dual you 
anyway on their next turn and hurt you badly or kill you. So it's better that 
you do maximum damage to them first. Any time you're definitely going to 
die on your next turn, you probably should use it.
<p>
PS: Be careful you don't try to get so deep inside the enemy that your 
shot actually misses their core completely. This happens a lot when 
Turtle's on an upward slope. 
<p>
