<body>First, the Fixed 70 backshot guide. I've been using this method since I was probably metal axe. Discovered it by accident after shooting backshots in every tailwind I met when I started using boomer in Dec 2003. When you get good with this method, expect to hit with over 90% accuracy in all winds. This is NOT a formula. I call it a measure-and-feel method.<br>
<br>
The 70 backshot method's main attribute is this: Using angle 70 under all BASE WINDS, the length of the power meter needed to hit is exactly equal to the HORIZONTAL DISTANCE between your boomer and your target.<br>
<br>
There are two ways to measure. For example, if your boomer is left of the target, using right-click to drag your screen around, align the start of the power bar to the middle of your boomer. Wherever the target is on the power meter, that’s the power you need to shoot at.<br>
<br>
The other way to measure is to use a very sophisticated tool...your fingers! Measure the boomer-target distance using your thumb and finger like calipers. Transfer that to the power bar and presto, that’s your power for a BASE WIND.<br>
<br>
For a non-base wind, you need to add or subtract some power. Those familiar with Wind 0 standard powers for a fixed angle and adjusting for wind, this would be a similar concept. But whereas Wind 0 in a fixed angle method is only one, our BASE WIND in a 70 backshot situation are a number of different values in a number of different wind directions.<br>
<br>
So which winds are base winds? And how much power to adjust for each? You can refer to this wind chart, for your winds and the corresponding amount of power adjustments. Where it says “EQ” (for equal), that is a BASE WIND. For example, upward Winds 5-14 are base winds.<br>
<br>
Power adjustments are measured as a fraction of the initial measurement, NOT fraction of bar. So if base wind power is 2.5 bars, and the wind says to "less by 1/3", just visualize chopping the 2.5 bars into three and power up to the start of the 3rd piece. (At least that's how I mentally do it...) You can also just use your “feel” to adjust for wind, as long as you know when to add or subtract.<br>
<br>
I don't use this picture myself when I'm playing, since I've gotten a good feel for backshot winds (as you will, if you use angle 70 almost exclusively). But I created this image initially to share with a very special friend of mine. Now I'm sharing with you. You may have to refer to this chart for your first stab at this method. But try to get a good feel for (or memorize the) adjustments.<br>
