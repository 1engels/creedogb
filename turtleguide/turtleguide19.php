<p>
So far my most useful markers are the 1/3, 1/2, 2/3, and 1 screen shots.
Another somewhat useful measuring trick is to count 1 angle of skybomb
as a bit more than 2 angles using the 2.4 (30 part) formula. So an enemy 
who is about 6 parts from the end of the screen would be about 3 angles 
higher than your 1 sd skybomb, just keep in mind that they're actually 
a little less than 3 angles away from the end of the screen (because each
skybomb angle is a bit more than 2 out of 30 parts)
<p>
<img src="skybombdistances.jpg">
<p>
Also, even though a true test shot is not possible, you can try using 
the 3.4 formula shown earlier to give you an idea of where the skybomb 
will land. That formula uses 15 angles per screen. A skybomb uses 14, 
so it's pretty close. A more accurate test method might be to use 3.45 
for close shots (near half screen) and 3.5 for 1 screen. If there's wind, 
you would try to adjust your 3.5 'test shot' the exact same way you'd adjust a skybomb,
like if the windchart says to add .2 bars, a half screen test shot with shot 1 or 2 
should be fired with 3.65 bars, and your halfscreen skybomb would (in theory) 
use the same angle with 3.92 bars. It's not perfect but it's good if you 
have a chance to beat the opponent's delay and really have no idea 
what angle you should use for skybomb. Another tip: at full power, 
skybombs travel more or less the same distance that nak's shot 1 does
(so 77 full would be 1 screen).
<p>
Ok... here's my simplified windchart:
<p>
<img src="skybombchart.jpg">
<p>
Some quick explanation: This chart shows you how to change
both angle and power. To make a good skybomb when there's
wind, you must change both. Upwind gives a shot more airtime, and
you always want the skybomb to open at its peak, so you must
decrease power to compensate. Downwind decreases airtime so
you must increase power to make sure it 'peaks' later.
Opposite wind also decreases airtime and requires adding a little bit
of power. However, tailwind does not change airtime and requires
no power change. You also don't need to change power if there's
opposite wind that's pointing a little bit upward.
<p>
The chart's large numbers tell you how to change your angle.
This is just like the windcharts you've seen before. The numbers
are all pretty easy to work with. Both tailwind and opposite wind
have a factor of .53. A simple shortcut is to divide the wind's strength
by 2, which would be fine for a wind factor of .5... since this is a little
stronger than .5, you will see that tailwind blows the shot a bit further
than expected, and opposite wind holds it back a bit more.
In small winds (10 and less) this doesn't matter much, the skybomb should
still hit. But in medium winds (10-20) the shot will miss by half an angle.
You can compensate by walking forward a bit (when
dealing with opposite wind) or walking back a bit (when dealing with tailwind).
In 20+ wind, just raise or lower 1 more angle, i.e. in 22 tailwind raise
12 angles instead of just 11.
<p>
Other shortcuts: The down+forward factor is close enough to Wind/3, so
in 12 wind you'd raise 4 angles. The up+back factor is pretty close to
Wind/2, it's a bit less though. You'd lower 9 angles in 20 wind instead of 10.
The up and down factors are tricky. In vertical wind, you will lower 1 angle
for every 15 upwind (so in 26 wind, if you lower 2 angles the shot will fly a bit
past your target). In downward wind you raise 1 angle every 20 wind... BUT
in downwind you can't skybomb when the wind is stronger than 9 or 10. The
shot is forced downward too much if the wind is stronger than that. So in 10 wind
just be aware that your shot will land a half angle further than expected, because
adding .3 bars of power increases the distance by that much, even with 10 wind
forcing it down.
<p>
The smaller italic numbers are your power factors. The easiest ones to
work with are the upward and downward factors. For every 3 wind strength you
should lower .1 bars (upwind) and add .1 bars (downwind). So in 9 wind down,
you'd add .3 bars, so a 3.7 bar shot becomes a full power 4.0 bar skybomb.
That's why 9-10 wind is considered the max for a skybomb in downwind.
Upwind has no max. Since it's .1 bars every 3 wind, you more or less divide
wind by 3, then reduce power by .[answer]. For example, in 24 upwind,
divide by 3 to get 8. You should reduce power by .8 bars.
<p>
The next easy adjustments are for up+towards and down+back, because
they mirror each other. Add .15 bars for every 5 wind in down+back wind...
so in 10 wind, add .3 bars. For up+toward wind, reduce .15 bars for every 5 wind.
So in 25 wind up+towards, you'd reduce a whopping .75 bars (so a 3.7 bar skybomb is
only 2.95 bars in this wind).
<p>
Down+toward and up+back wind also nearly mirror each other. Reduce .2 bars
every 10 wind for up+back wind, and add .2 bars every <b>9</b> for down+towards.
There's only one thing that makes this a bit confusing: The up+back wind works like upward
wind on the shot, so you must reduce power to give it the correct airtime. At the same time,
you must lower your angle to make it travel far enough, because you're reducing power
so much. Wind pointing straight up is the same way. Downward and down+towards wind
both require adding angle, because you'll be increasing power so much that you must
raise angle to keep the shot from flying too far.
<p>
Lastly, the opposite wind factor, .008, boils down to this: add .1 bars for every 14 wind,
so in maximum (26) wind you would add a hair less than .2 bars... so a 2.7 becomes about
2.9 bars in the strongest opposite wind.
