Now - the one screen chart. This chart is easier because the basic angle for 
1 screen is nearly angle 45, and somehow the math for angle 45 tends to create 
very simple numbers and power levels. Up+back and down+forward wind 
require nearly no power adjustment, and up+forward and down+back 
require almost no angle adjustment.
