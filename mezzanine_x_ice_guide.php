About Shot Origin:<br>
The way this guide will later teach you to shoot high angle and ultra high angle shots. This is for a good reason. Land will rarely block your shot. Shot 1 should never be blocked, unless you have a ceiling. However shot 2 and SS are another story. If ice is pressed up against a wall and its nose is in the ground, you cannot shoot, or you will hit yourself! My tactic to overcome this is simple. They need a high angle to hit you, or else they will hit the land in front of you and clear you a shot. SO... if you use shot 1 to screw their angle by shooting the mountain the are on, you have the superior angle, and they can't hit you. They will probably TRY to shoot you anyway, and clear the land in front of you (giving you a shot) or will not be able to hit you. If you find that the wind is strongly with you and you can't hit them at all because they make a cliff that looms over you, (like a JD trapper) move to the opposite side and do a backshot.<br>
