<h1>3. Lob/fixed power shot</h1>       
<hr>
<blockquote>
This follows a simple formula. It can only work within 1 screen distance. Use 
about 2.2 bars of power, and simply change your angle to hit different areas 
of the screen. You actually vary your power a little depending on how far 
away the enemy is. I used to use the same power all the time but realized I was
missing a lot of seemingly sure shots that way. I figured out this method:
<p> 
<table border=0 cellspacing=0>
 <tr>
  <td>1/3 screen or closer</td>  <td> 2.15  bars</td>
 </tr>
 <tr>
  <td class=type2>between 1/3 and 2/3</td> <td class=type2> ~2.2 bars</td>
 </tr>
 <tr>
  <td class=type3>past 2/3 screen</td> <td class=type3> 2.3  bars.</td>
 </tr>
</table>
<p>
Anyway, you will learn to make the tiny alterations automatically as you play.
You can almost get away with using 2.2 all the time but you'll find you cannot
quite go 1 screen distance without a bit more power.
<p>
<h3>LANDMARKS</h3><br>
90 hits yourself (of course) in 0 wind.<br> 
80 hits 1/3 screens away.<br>
75 hits half a screen away.<br>
70 hits 2/3 screens away.<br>
60 hits 1 full screen.<br>
<p>
