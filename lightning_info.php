Here's what I know about lightning angles. You don't need to know all of these, just pick a shooting system you like (fixed 60, fixed 30, fixed power with 30 parts, fixed power with 20 parts) and memorize those. The first 3 systems are accurate but only go 1 screen. The last one works up to 1.5 screens but you cannot get perfect accuracy as easily, because you use so much power. I recommend learning the first two. If you learn angle 60, you automatically will know angle 30 because it uses the exact same power levels. Angles like 60 and 30 are easy to get, angles like 70 and 80 are harder. <br>
