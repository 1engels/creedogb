SS<br>
Yes, the juicy stuff, one of the most lethal shots in the game. There are 3 ways of pulling this shot off, that I know of.<br>
<p>
1) Sticky.<br>
This works in downwind and on flat land. Walk right up next to them and tap, the ball should stay pretty much where it lands. It's best to use a reasonalbly high angle for this to ensure it doesn't go rolling about.<br>
Another way is if they are pressed against a wall and there is strong wind pushing into the wall, the ball should pretty much stay pressed against the wall with your enemy.<br>
<p>
2)Bouncy.<br>
Works in reasonably flat ground. This is where you use as high an angle as possible which means often full power. The ball goes up, comes down and bounces on the spot dealing some nice damage. Watch out for the roof though.<br>
