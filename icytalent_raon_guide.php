Gameplay<br>
<br>
<br>
Raon can be a supporter or a killer in the game. I will explain the uses of each shot.<br>
Usually a high angle would be recommended when no shotgunning is involved.<br>
<br>
<br>
Shot 1<br>
<br>
Shot 1 is often used as a test shot, bunge shot, or a shot to combo with shot 2.<br>
Using a high angle works a lot better than using a low angle, because shot 1 eats land. Hitting with a high angle right in front of an enemy can put him into a ìniceÅEhole, where he might lose his angle. Angle killing is a very important technique when you are up against an asate or armor.<br>
Shot 1 can be used to beat delay too. In situations when shotgunning a shot 2 is possible, but the enemy has like 600 hp left, you might be able to do a shot 1 first to beat his delay, then dual shot 2 next turn.<br>
In other situations, you might be able to use this method to get an extra shot 1, then dual shot 1 next turn.<br>
Note: Avoid tapping a shot 1 because you will tend to take some damage due to the area of the shot.<br>
<br>
<br>
<br>
<br>
Shot 2<br>
<br>
There are many ways to play this killer shot. The simplest is to shotgun, by aiming directly at the targetís feet, not too low and not too high. A successful shotgun will cause the 2 mines to explode on contact, causing damages around 250 to 300+. If you have a sure-hit shotgun, dual it if delay doesnít matter, but always remember, the target mustnít fall down after the first shotgun.<br>
You can surround a target with 2 mines with shot 2, which will make them trapped if they donít wish to take any damage.<br>
Like this - <br>
<img src="icytalentraon1.jpg"></img><br>
For this method, the higher your angle is, the less spread there will be. Spread refers to the distance between the 2 mines upon landing. To make the 2 mines stay as close as possible together when they land, the best would be angle 90, or angle 0. Here is a simple graph to show the spread of the mines depending on angle.<br>
<img src="icytalentraon2.jpg"></img><br>
The closer your angle is to 90, or 0, the lesser the spread will be, therefore, I use a high angle for shot 2 whenever possible.<br>
The closer your angle is to 45, the larger the spread will be, which means that the 2 mines will be very far apart, which can be bad, but it can be used as a method to set mines on 2 close targets.<br>
Thus, the spread of shot 2 mines depends on the angle, not the wind.<br>
<br>
Shot 2 can also be used as a preemptive attack. You can lay mines on the spot where an opponent is going to land, thus having an advantage when he lands.<br>
<br>
If you dual a shot 2 which doesnít explode on contact, and doesnít surround the target, the target can easily move away to avoid the mines, if he has enough movement. Thus, combining shot 2ís effect with shot 1 can prove to be useful.<br>
<br>
There are 2 ways to combo shot 2 with shot 1, using dual+.<br>
First, it is to use a dual+ with shot 1 first. With an accurate hit, the target will fall into a hole with 2 mines surrounding him, allowing little or no escape. This strategy is quite common.<br>
<br>
The second method is to use a dual+ with shot 2 first. This is an uncommon method, but I like it. It works best with high angles, and I mean really high ones, like 80+. The aim is to make the mines land very close to the enemy, cramped together, then making both the mines explode with the following shot 1, causing more damage, such as 450 =)<br>
<br>
Here is an example (I took the screenshot before the final damage was shown, which is around 390)<br>
<br>
