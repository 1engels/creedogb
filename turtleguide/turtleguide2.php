<h1>Turtle's weapons:</h1>
<hr>
<blockquote>
<h2>Shot 1</h2>
A basic shot 1. You can use it to beat someone's delay and get 2 turns
in a row on them (or make sure they don't get two turns in a row on you). You
can also use it to kill an enemy with only a little life, which gets you the
kill and saves some delay. It makes a small round hole, and if
the enemy has a lot of land in front of them, and you place shot
1 right in front of them, it ruins their angle. It's also the best shot
to use when you are not sure you can get full damage from shot 2.
A shot 1 is stronger than a half-hit from shot 2, and uses less delay.<br>
<h2>Delay: 740</h2>
<h2>Typical damage: 150</h2>
<p>
<h2>Shot 2</h2>
Shot 2 is trickier to use. It fires two streams of water that spiral
through the air. The streams come from Turtle's cannon clustered together,
then split apart, then come together again, and they keep repeating
this fast spiral pattern 5 or 6 times. After enough time in the air (about 2.5 seconds),
the streams come together one last time and <b>stay together</b>. That
means that after a lot of airtime, the two streams become one powerful shot
that does high damage. There are actually 3 ways to get this kind of full damage:
<ul>
<li>Keep the shot in the air 2.5 seconds or more.
<li>Shotgun up close, so that both streams hit before they can split apart.
<li>Using either luck or skill, time it so that both streams are crossing together<br>
at the moment they hit the enemy. This is called a fork shot or just "fork".
</ul>
<h2>Delay: 890</h2>
<h2>Typical damage: 260</h2>
<p>
<h2>SS</h2>
This is the most difficult shot to use in the game, which makes it very satisfying when
you can hit with it. It's one of the strongest in the game when it hits for full damage.
This is the way it works: The shot starts out as one big water blob.
After about 2.15 seconds in the air, the blob splits into 6 smaller bombs.
The bombs immediately fan out in a spread pattern that will remind you
of the spread gun in the old NES game Contra. If you can somehow get all
6 bombs to hit the same enemy, you'll do a lot of damage.
<br>
There are a few ways to do that... which I'll cover later.
