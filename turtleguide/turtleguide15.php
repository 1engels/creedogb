<h1>Banpao (3 bar) formula</h1>
<hr>
<blockquote>
Banpao is a better formula than 2.4, but it's a little harder to use.
The power level is higher, so there's a larger distance
between angles, and that increases the chance of picking the wrong angle
or getting only a half-hit. It also means that if you get the power wrong,
the effect is greater.
<p>
With 2.4, you can get away with using just 1 windchart 
all the time. It's not perfect, but with experience
it can be good enough, and I try to show the exceptions to the rules.
The shots are low power so you 'feel' them a bit.
With banpao, you really must be more careful with wind adjusting. The more
power your shot has, the more airtime it will have the greater the wind's effect will be.
For example, if the math says to lower your angle by 8.5 degrees, and you
lower only 8, your 2.4 shot can still hit the front of the target. But with banpao,
being off by half an angle is enough to cause a complete miss.
<p>
Still, despite all of this, banpao is a very nice shooting system for Turtle. 
The power level is high enough to make shots beyond 
one screen (it works up to 1.5 screens). It also is nice 
because when you're deep in a pit, or the enemy is deep in a pit, you
will need to use a high angle shot to hit the target. Banpao uses higher angles
than 2.4 - for example a half screen shot is angle 80 instead of 75.
Some players say not to use 2.4 at all and just learn banpao. I think
2.4 is useful though and it's very similar to banpao. It's like a 'training
formula' to get you ready for banpao.
<p>
