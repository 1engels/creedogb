<body>Boomer Reverse hookshot<br>
<br>
- I don't have a good solid formula yet but I'll share<br>
the little bit of info I know. I think this is most useful when you<br>
want to backshot someone but are stuck on a slope and cannot<br>
get a low enough angle to do a normal backshot.<br>
<br>
Power = 3.0 bars fixed<br>
<br>
Angle: <br>
80 = 1/4 SD in weak wind<br>
77 = 1/3 SD<br>
Half screen is difficult without strong wind.<br>
<br>
The higher your angle is, the closer your shot lands. The highest<br>
angle you'll want to use is 88 because angle 89 against the wind<br>
always will hook forward.<br>
<br>
The stronger the wind is, the more you must raise your angle to<br>
prevent the shot from flying too far, for example in strong<br>
wind I might use 81 or 82 for 1/3 screen.<br>
<br>
Past a certain point, lowering your angle can also make the shot<br>
go further. To make things more confusing, sometimes the shot will<br>
hook backwards, and then with just 1 wind difference it will hook<br>
forward. Or for example angle 85 might always fly back, but 84<br>
will fly forward, but then 83 will also fly back. It's a very<br>
unreliable shot and that's why it's hard to make a formula for it.<br>
<br>
Good luck with it. Remember you don't look cool unless you're<br>
facing forward when you do it :D<br><br>
