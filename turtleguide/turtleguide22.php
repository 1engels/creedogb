<p>
I rounded most of the powers off to the nearest .05 bars. 
If your power control is very sharp, it's possible to slice in smaller
increments, and for most of these SSes your shot will be a little
tighter (and the damage a little better) if you can shave off 2 or 3 pixels
of power. For example a true pointblank half screen timebomb is 66, 1.97.
The shots over 1 SD I gave exact powers because it's necessary.
The angles are also rounded up to the best approximation, but they
aren't perfect. For example an enemy at 2/3 screen is really
halfway between angles 60 and 61, but that's too much detail to get into.
By using the angles given here you'll still get a good SS.
<p>
When an enemy isn't at any of these landmarks, you can still hit them.
You just need to find the exact right angle. Remember that
angle 48 is needed for 1 screen. Between 1 screen away
(48) and yourself (90) there are 42 other possible angles to use.
So there's no place you can't hit with a timebomb. The numbers
given in the tables above are just guidelines to choosing the correct
angle out of those 42.
<p>
To hit an enemy who isn't at one of the landmarks, look for
the landmark nearest them. Then estimate how many
angles you need to raise or lower to decrease or increase
the distance of your shot. There's a trick you can use for this:
Turtle's body is about 3 "ss angles" wide. So an enemy who is
1 'Turtle body' past the half screen mark can be hit with angle 63.
You know angle 66 needs 2 bars, so angle 63 probably needs
a hair more than 2 (the lower the angle, the more power is needed).
<p>
<h2>Elevation adjustment</h2>
<p>
This is tricky, even for experienced players, and I don't know of
any fast way to calculate it. When you shoot an enemy who is
higher than you, you must add power. By adding power, you
are making the shot fly higher into the air, but you are also making
it fly further forward. Having it fly higher is necessary to get the right
airtime, but having it fly further forward is not a good thing. If adding 
the right amount of power is going to make it fly too far, 
then what can you do?
<p>
Well, remember that for timebombs and similar shots, power
controls airtime and angle controls distance. If adding the power
you need makes a shot fly too far, then you must raise your
angle to prevent that. Similarly, if the enemy is below you
then must reduce power to get the right airtime, but then
you risk having the shot not flying far enough. To
prevent that you must lower your angle to give the shot
added distance.
<p>
So the bottom line:<br>
<b>
Enemy below you = lower angle, less power.<br>
Enemy above you = higher angle, more power.
</b>
<p>
The only question then is how much angle and power?
It depends on how far away the enemy is. 
The further they are, the more angles
you must adjust. I've made a chart. To be honest,
I never memorized or even confirmed these numbers
before making this guide, I've always adjusted for elevation
with "feel" before. But I know these numbers are accurate 
because they've been calculated. See the faq at the end
of this guide to learn how. Also, for now I will just give
the angle adjustment. You can feel the power difference
pretty easily, or you can calculate it yourself. The numbers running
across the top are the enemy's horizontal distance from you.
The rows below show the adjustment when they are elevated
1/4 and 1/2 screen in either direction. If you're unsure how
to measure elevation, press escape during the game to see
your options box. The options box is exactly 1/4 screen tall,
which means 200 pixels (thanks to Yoyobuae for sharing this).
