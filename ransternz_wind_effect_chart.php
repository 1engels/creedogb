I'm so sick of pure feel players missing by a mile because they have no idea how much affect the wind will have on their shot. So here's my bid to try to stop this.<br>
<br>
Warning<br>
If you don't want to be confused by numbers, just look at the wind chart and ignore the rest.<br>
<br>
Basic rules:<br>
<br>
The wind bearings at which the wind will have most affect are (90-A) and (270-A)<br>
<br>
The wind bearings at which wind will have no (or very little) affect are (180-A) and (360-A)<br>
<br>
Where A = angle at which you're shooting.<br>
<br>
Visual aide.<br>
<img src="feelchart.jpg"></img>
<br>
eg:<br>
You're shooting with an angle of 40.<br>
The wind bearing that will make your shot travel the most will be (90-40) = 50<br>
The wind bearing that will make your shot travel the least will be (270-40) = 230<br>
At bearings (180-40) = 140 and (360-40) = 320 the shot will land as if there was no wind.<br>
<br>
Notes:<br>
<br>
Wind at bearings (180-A) and (360-A) will only travel as if wind was 0 on even ground. Due to the nature of the flight arc, you will have to decrease/increase power for elevation more/less than you would if it were actually in 0 wind.<br>
<br>
Opposite bearings do not necessarily have opposite affects.<br>
Due to the exponential nature of the power bar, opposite angles (eg: 250 and 70) will not have opposite affects.<br>
For example, if we're shooting at an angle of 40 one screen away, power to hit in 0 wind is 2.35. If we have 24 wind at angle 250, we need to use 2.66 power, whereas if wind is at angle 70 we need to use 1.99. As we can see (2.66-2.35) .31 < (2.35-1.99)<br>
.36 .<br>
<br>
So..<br>
<br>
Tailwind will always have more affect on the power needed to adjust than headwind will, as with tailwind you're decreasing power and tailwind increasing. Again, this is because of the exponential nature of the power bar.<br>
