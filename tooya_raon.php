Shot 1<br>
Average Damage<br>
180<br>
<br>
Delay<br>
250 (750)<br>
<br>
Shot 1 is one of the weakest shots you can find damage wise, however land damage it is god. Most people only end up using shot 1, thinking shot 2 is ineffective, these people I smack silly with a tampon. <br>
<br>
Shot 1 should only be used for 5 reasons:<br>
<br>
Bungeing<br>
Holeing an enemy<br>
Test Shotting<br>
Avoiding Delay<br>
Making a nice angle for your shot 2<br>
<br>
If your plan is to bunge an enemy, try as much as possible to hit the land in front of the enemy and not the actual enemy. If your shot hits the enemy dead on, nearly no land damage is made. The enemy absorbs the yoyos. :(<br>
<br>
