<h1>Strengths and weaknesses:</h1>
<hr>
<h2>PROS:</h2>
<ul>
<li>High attack strength, one of the best attackers.
<li>Best defense rating, can survive 2 weak duals. 
<li>Decent delay for a high damage bot. Turtle is a lot like Armor without delay penalties.
<li>Flexible shot that doesn't get weaker at long distance, and usually is ok with a nado.
<li>If you already know armor formulas and shots, Turtle's shot is the exact same weight<br>
so it's easy to learn.
<li>Strong as a shotgunner. Some bots suck for shotgunning but Turtle has no problem.
<li>Easy to get a high angle without a lot of slope, because it's placed so high.
<li>Fun to use. Forks and SSes are challenging but you don't HAVE to use them.<br>
People always love a nice SS.
</ul>
<p>
<h2>CONS:</h2>
<ul> 
<li>Tiny, slow walking range, it can take Turtle forever to reach an upward slope.
<li>On flat land it can be difficult or impossible to make a nice shot 2 for full damage.<br>
Turtle usually needs a highish angle to guarantee good damage.
<li>Very little bunging ability - your shot doesn't disturb the enemy position much.
<li>Shot doesn't blast through land easily, and you'll have a hard time when angle is ruined.
<li>Shot comes from Turtle's center, so if Turtle cannot walk a bit past an enemy's dead body,<br>
any shots you make will hit that body and hurt you. This also means Turtle cannot really use<br>
the ass-shot technique when his shot is blocked...it won't change the firing point of your shot much.
<li>Your shot's explosions are tiny. Other bots can miss the enemy a bit but still splash them<br>
for decent damage. Turtle's near-misses do very small damage so you need to be accurate.
</ul>
</blockquote>

