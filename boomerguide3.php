<h1>Other strengths and weaknesses:</h1>
<hr>
<h2>PROS:</h2>
<ul> 
<li>Good movement (climb and move distance).
<li>Huge aiming reticle - you can achieve any angle you want at any time.
<li>Pretty good gold-earning ability - lots of backshots, high angles, etc.
<li>Fun to use. Impresses people when you make nice shots. 
</ul>

<p>
<h2>CONS:</h2>
<ul> 
<li>Low HP/Defense - Boomer can die in two duals vs many mobiles.
<li>Can be difficult to use - wind strongly (and strangely) affects your shot.
<li>No bunging ability to speak of - your shot doesn't disturb the enemy position.
<li>Boring because everyone else plays him.
</ul>

