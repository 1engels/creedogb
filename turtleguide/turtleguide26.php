<h1>Part 4: Other SS Tips</h1>
<hr>
<blockquote>
It might seem like this kind of calculation is impossible to do
without a lot of time, or that there are too many variables. There's 
some stuff you can do to make it easier though:
<ul>
<li>By far the most useful advice I have is to calculate the angle only 
and then try to feel the power level. Even with my horrible feel, I've
been able to get very nice SSes this way. It's especially important for
timebombs with elevation. Even if you feel the correct power to hit
the enemy, it isn't a guaranteed thing because the same shot can
hit an enemy with .05 less (and sometimes .05 more) than the 
required power. If you have a lot of time, calculate the power also. Most 
of your thinking is done between turns, not during your 15 second 
shot clock.
<li>In fact, if you try to memorize everything without using 'cheatsheets' 
(as I do) then just memorize the wind factors for angle change first. And
don't try to take in everything at once, start with the 9 major factors then
learn the smaller in between factors.
<li>Try the Jewel Mode training technique mentioned earlier. You 
can stretch a single game out to half an hour. If you try SSes only in 
a game, you will be lucky to get 1 shot every fifteen minutes. But 
in Jewel SSdeath mode you can try a shot every few seconds and 
have tons of targets.
<li>If you don't know the power at all, try a little more power than you think.
It's better if the timebomb opens and gets a crappy half hit than if it
doesn't open at all.
<li>Some sharp players I know do things backwards and calculate 
the power first, then try to find the angle. If this works for you, then 
that's great. The advantage to it is that if you DO get the right angle, the
ss will be really sharp and damaging. For certain winds, I use this 
method as well - Winds that are pointing mostly down and a bit back,
mostly up and a bit forward, and any winds that mirror these two 
areas are tricky to calculate the angle for, because the wind direction
may be very close (or be directly opposite) your original shooting angle.
For these winds I will test shot first with the chosen power level then 
adjust angle as needed.
<li>If the game isn't super serious, (like a solo game where you get one shot) then
go ahead and use a test shot for your timebomb. If I'm ever the last player to shoot 
(it happens to me all the time) then I will try to do a quick shot 1 and beat
the delay of all other players so I can test for my SS. Just be careful of 
situations where a nado may pop up between you and the target. Doing 
an ss right before a nado is a good idea for the same reason that doing
a dual right before a nado sometimes is good: To punish your big delay 
the enemy will have to make a tricky nado shot.
<li>If you can't get a low enough angle to timebomb, remember you 
can also do a skybomb, and vice-versa.
<li>There are two ways to test shot for an SS: shot 1 or shot 2. Shot
1 uses less delay, but shot 2 will actually give you a clue as to whether or not your
SS airtime is correct. When shot 2 spins through the air, it spirals 6 times. 
The middle of the final spiral is the point where the SS will open (just before
the shot stops spinning and starts to come together).
<p>
