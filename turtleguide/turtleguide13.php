<h1>2.4 formula - Part 3</h1>
<h1>'Funny' Winds</h1>
<hr>
<blockquote>
Some wind directions are more difficult to adjust for than others, because the original
shooting angle, distance to target, and other things matter a lot. When the wind is
blowing in these directions, your shot travels almost the same distance as it
would in 0 wind. Depending on how far you're trying to shoot, the same wind
might hold your shot back, not affect it at all, or make it travel further.<br>
I call these winds 'funny' winds.
<p>
The idea that the same wind can make your shot go either further or shorter 
probably is confusing. It helps first to understand how wind works in Gunbound.
When you shoot, your shot is moving with a combination of upward speed and forward
speed. Some winds will affect the 'upwardness' of the shot more, and some winds
affect the 'forwardness' of the shot more. Once you learn to think of the shot as being
a combination of upward velocity and forward velocity, you can learn to think of the
wind as being the same way. How those 2 velocities combine will determine
how much the wind affects your shot.
<p>
For example, wind pointing directly towards
will make the shot fly further, right? But what if it's pointing towards them and also 
pointing a little bit up? Then it will go even further, because the more upward
the wind is, the further your shot will travel... up to a point.
<p>
This wind: <img src="wind1.gif"> will make your shot go further than this wind: <img src="wind2.gif">
<p>
This is true even when wind is pointing against you. Wind that's pointing against and a little upward
will go a bit further than wind pointing directly against you, even though they both are going
to travel a shorter distance than your normal 0-wind shot.
<p>
In general, the more upward wind points, and the more forward wind points,
then the further the shot will fly. The more downward and the more against you the wind is,
the less far it will fly. But that doesn't mean that wind pointing up+forward
at a 45 degree angle will carry the shot furthest, and that wind pointing down+back will hold it
back the most. The wind direction that carries your shot further than all other wind directions
(for most Turtle shots) will be between the 45 degrees (up+forward mark) and the mark
that points directly forward. The opposite area (between back and down+back) will hold it back most.
<p>
<img src="maxwindeffect.jpg">
<p>
The wind that has the least effect will be opposite these areas, or to be more
exact, perpendicular... like this:
<p>
<img src="minwindeffect.jpg">
<p>
To make it more clear, I'll illustrate with a different angle.<br>
Here's what wind pointing at 55 degrees looks like:
<p>
<img src="55wind.jpg">
<p>
Now imagine that 55 degree line being reflected on the other side
of the wind marker.
<p>
<img src="55windreflected.jpg">
<p>
This reflected wind angle (the yellow line) will have the least effect on your shots
when your shooting angle is 55. At any wind strength, even 26 wind,
when the wind is pointing in this direction it won't affect your angle 55
shot at all. This angle can be calculated: Mathematically, a wind circle 
is best shown as 360 degrees, and the line pointing perfectly right is 
angle 0. You then go counterclockwise around the circle to straight up (90), straight
'back' (180), straight down (270), and back to forward again (360, aka 0).
<p>
The 'no-wind-effect' point can be calculated as '360 - shooting angle'.
What's neat is that the exact opposite point on the chart, which is calculated
with '180 - shooting angle' will also have no effect on the shot:
<p>
<img src="55windmin.jpg">
<p>
Once you know the 2 points with <b>minimum</b> effect on your
shot, the 2 points with <b>maximum</b> effect are also easy to spot.
They are perpendicular to the minimum points and are calculated with 
'90 - shooting angle' and '270 - shooting angle'.
<p>
<img src="55windmax.jpg">
<p>
So how does this apply to the 2.4 formula? Well, you know the formula
uses angles between 90 and 60, but most of your shots will be in the 70's.
So the picture shown earlier for 70's minimum and maximum wind should
apply to most 2.4 shots.
<p>
<img src="minwindeffect70.jpg">
<p>
Looking again at this minimum wind picture, you can see there are 2 wind directions
that can have no wind effect for angle 70: up + a little bit back, and down + a little towards.
Again it is helpful to try to think of this wind as a combination of vertical wind and
horizontal wind. At lower, flatter angles... vertical wind has more effect, and left/right wind
has less effect. At higher angles, it's the other way around.
<p>
For the up+back wind:<br>
- If the angle you plan on shooting with is under 70, this wind acts more like
upward wind and carries your shot a little further than before.<br>
- If the angle you plan on using is over 70, then the wind holds your shot back
a little bit.
<p>
For the down+towards wind:<br>
- If your shooting angle is under 70, the wind acts like downwind. The shot
lands a little shorter than before.<br>
- If you plan on shooting with an angle higher than 70, the wind carries the
shot forward a little bit.
<p>
If this isn't clear, don't worry about it. I got to be pretty good with Turtle
without understanding it. But understanding it helped my Turtle get a bit
better, especially with SS and fork shots.
</blockquote>

