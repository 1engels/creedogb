<h1>Turtle SS</h1>
<h1>Intro to Timebombs</h1>
<hr>
<blockquote>
The other way to get nice damage from the SS is to timebomb it.
That means you must try to control the airtime of the shot so that
a split second before it splits, it is in the air just a few pixels away
from the target. When the shot splits open, the 6 water balls all
fly into the opponent while they're still clustered close together.
They don't get a chance to spread out.
<p>
<img src="timebombafteropen.jpg">
<p>
This is difficult, but not so difficult that it isn't worth trying.
With practice, you can hit these with 90% accuracy inside
1 screen with no wind. Wind makes things more challenging,
but there's no reason you can't hit at least half your timebombs
with the right information and some practice. Some advantages to
using timebombs:
<ul>
<li>Being off a little bit can still get you some damage. I often
get about 250 from a timebomb that's one angle off, or overpowered
a little bit.
<li>Skybomb is limited to a little past 1 screen distance. Timebombs
can hit at any distance.
<li>Since timebombs are lower power, adjusting for wind is a little easier.
Being half an angle off with a timebomb is no big deal, but being half
an angle off with skybomb is a sure miss.
<li>The enemy cannot be 'between 2 angles' with timebombs, but
with skybombs it happens all the time and you're forced to
walk to compensate.
<li>Timebombs don't need very Long forks so they're usually available
with only a little slope. Skybombs often are not available because of
lack of angle.
<li>You may not agree, but I think timebombs look cooler :D
</ul>
So, what's the secret to timebombs? Well, you won't like the answer but
it's work and memorization. For some bots, you can skip using formulas
completely, or you can get by with some feel.
But for Turtle's timebomb you absolutely
have to memorize some stuff and do some calculation. That doesn't
mean you must use a ruler or calculator. I do all my measuring and
all my calculating in my head, without outside help. But whether
you use help or not, you will need to deal with some memorized
numbers and a windchart. You can't 100% feel these and have
a decent success rate. If you're willing to do a little work, it's worth it,
because a nice timebomb is the most fun shot in the game.
<p>
Read on for the numbers. Don't be afraid to write them down or print
them out. If you and another friend are interested in learning this 
(or any other shot) there's one very good training method - start a 1v1
Jewel game, with only windchange items and maybe teleports available.
Put the game settings on SSdeath mode and 40 turns.
Pick an easy, fairly flat map like metamine or miramo town. When the game
starts, don't try to win. Instead, just skip turns until 40 turns have passed and
SSdeath mode begins. Then, instead of trying to aim for just the best jewels,
aim at each other, or aim at worthless -5's or 5's. Try to timebomb a variety
of targets, but avoid 25's because you're not trying to win, you want the
training session to last a while. Keep trying to timebomb each target until you
get the correct angle and power.
</blockquote>

