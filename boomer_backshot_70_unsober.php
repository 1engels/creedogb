LAST WORDS OF ADVICE:<br>
<br>
1. When the total power (i.e. initial measurement plus or minus any adjustment) reaches "very close to" 3 bars, your backshot will fail to reach your target, no matter what you try. Either use a lower angle to try your backshot, or turn around and just lob it. The "distance=3bar" hint is a visual cue for you. Heed it, and you won't look like a backshot noob ever.<br>
<br>
2. For elevation differences, visualize a 60 degree line running through your target (upper part inclined towards your boomer). Where the horizontal and the 60 deg lines intersect is where you start measuring to your boomer. Treat very minimal elevation differences (less than 1 cm height difference) as same elevation, so no further adjustment (apart from wind).<br>
<br>
3. For targets much higher than you, boost your power AFTER adjusting for wind. The higher the target, the more power you need.<br>
<br>
4. For targets much lower than you, lessen your power AFTER adjusting for wind. The lower the target, the less power you need. Beware though, when you get to as low as 1/4 bar of power and the wind is not an upward 20+, you may TK yourself!! Upward 20+ winds may carry the shot of 1/4 bar power over your boomer but it’s risky. Again, when you get this visual cue, abandon the 70 backshot.<br>
<br>
That’s about it. In time, you’ll be hitting 70 backshots in under 2 seconds in any wind. Have fun.<br><br>
