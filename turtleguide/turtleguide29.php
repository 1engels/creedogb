<li>How do you calculate all these things without using a calculator?
<p>
For almost any calculation there is a shortcut, like dividing wind/2 when you are 
calculating a .5 wind factor, or remembering that .7 means raising 7 angles for every 
10 wind. It's important to memorize your 0 wind info completely so that it takes no time 
at all to know the 0 wind angle and power for your shot.

<li>How do I divide a screen into "parts"? How can you measure exactly?
<p>
The first step is to memorize the little landmarks on your screen to help you 
remember certain shots. I tend to think more in terms of angles than parts. Like,
if I'm using the 2.4 formula and the enemy is 1/3rd of the screen away, I don't see 
them at "part 10", I see them at "angle 80". Then I can start calculating the wind 
adjustment right away. Also, some may feel this is cheating, but you can make 
a paper ruler and tape it under your monitor, or make a transparency and 
put it on the screen. But you should memorize the other ways of measuring too,
so if you ever visit a friend and play with them in a lan shop, or if you ever go
to a tournament, you don't look like a noob who can't measure properly.

