Wood's Boomer (and coincidentally, J.D.) High Angle Guide<br>
<p>
High Angles:<br>
Wind: Must be 0 or 1 wind.<br>
<p>
Where the thin yellow lines intersect is where a full powered shot of the angle indicated will land. For example, if I were to hit brenden720, I would use 83, full powered.<br>
<p>
Notes:<br>
1) If the wind is blowing at 2, and with your shot, not against, use 1 degree less. For example, if the wind is blowing 2 to the right, I would use 84 degrees instead of 83 to hit brenden720.<br>
<p>
2) Bolded yellow line on the bottom is an easy indication of an 87 full powered shot; the "[ALL]" button to the middle markers of the power-bar.<br>
<p>
3) An 85 degree shot can be measured by using your right click button and dragging the wind-indicator next to your enemy. It should be about half a centimeter away from it, indicated by the bolded yellow X. Same applies for an 86 shot, but on the other side of the wind-indicator.<br>
<p>
4) Do not use the "1 degree = 3 centimeter" rule, because it is not always the same for different resolution monitors.<br>
<p>
5) For 89 angles, try it once with 0 wind, see where it lands, and memorize how far away that is. This would be a "one degree length".<br>
<p>
6) For enemies that are more than 1 screen away, use common subtraction. If an enemy is 2 screens away, think of 1 screen as -9 degrees (90-81=9), so exactly 2 screens away would be -18, or 72 degrees (90-72=18). Rule applies for all angles.<br>
<p>
7) Note elevation. If your enemy is higher than you by a substantial (about half-screen vertically) amount, go a little closer. If your enemy is lower than you by a substantial amount, walk a little farther back.<br>
<p>
8) If you want to make a better approximation of an 88 degree shot, you can align your mobile relatively to your target's mobile according to the measurement in the upper right corner, indicated by the 88. A more exact description is that the left edge of your mobile touches the left side of the first item slot, and the right edge of your target's mobile touches the right side of the sixth item slot.<br>
<p>
9) With an almost-exact 88 and 87, and a near-exact 86 and 85, you can get an exact 84 and 83 by remembering where a 1-screen 81 is, then subtracting that distance by the distance of an 88 (2 degrees) for and 83, and subtracting the distance of an 87 (3 degrees) for the 84.<br>
<p>
10) It just happens to be so that J.D.'s (cakebot) high angle at 0 wind shots are either extremely close or exactly the same as Boomer's. However, it is rather difficult to happen to have such an angle and coincidentally be at the right spot for a full-powered shot, so adjustments might need to be made.<br>
<p>
11) Mammoth's high angles are exactly the same as the Boomer's high angles as well.<br>
<p>
12) With regards to 2 winds pointing up or down at a high slope, it either will affect your shot by 1 degree or it won't make any difference at all. Sometimes, if it's pointing diagonally upward and you're shooting with the wind, it is hard to tell whether you'll need to take off a degree or not, so just try once (and guestimate, of course) and the correction if necessary.<br>
<p>
13) If the wind is perfectly pointing left or right, pretend 1 wind strength is the same as 2, and 2 is the same as 3.<br>
<p>
14) For 3 or higher winds, refer to this:<br>
-I think the wind conditions have to be near-horizontal, but not too far from it, nor exactly horizontal. First, from your position, find the degrees you would do if it was 0 wind. Then move back (away from your target) 1 mobile's length (by that, I mean the width of your avatar). Then, add "Windstrength - 1" degrees.<br>
-An example:<br>
-If the wind was say 7, pointing left, and your target was at the left. Say your target is exactly 1 screen away from your standing point. Move right the length of a mobile. Normally, you would do 81 degrees, but Windstrength = 7, so 7-1=6, so add 6 to 81, you would do a full powered 87 shot to hit him.<br>
-I'm not 100% positive about this, but it has been working for me most of the time.<br>
-If the wind is pointing slightly lower than horizontal, it won't go as far, and if it's pointing perfectly up-right or up-left, it will go farther.<br>
-I'm sure this method works for 3-5 winds, but I haven't tested with higher winds than 5.<br>
