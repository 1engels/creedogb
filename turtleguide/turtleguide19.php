<h1>Turtle SS</h1>
<h1>Part 1: Skybombs</h1>
<hr>
<blockquote>
Turtle's SS is pretty unique because you must control the airtime of the shot.
Some shots have a minimum airtime, like Armor's ss. But what makes Turtle's
difficult is that there is a minimum and a maximum airtime. If the shot hits
too early or too late, it's a weak, lousy hit. The airtime must be just right
for the shot to get good damage.
<p>
There are 2 ways to get good damage with the SS (in most situations).<br>
First I'll cover the easier method. It's not my favorite method, but it's probably
the most powerful and reliable way.
<p>
<h2>Skybomb</h2>
Sometimes called a 'line' SS or 'high angle SS' or 'Type 2 SS'.
<p>
This SS is fired with high power and a fairly high angle. The shot
splits open after 2.2 seconds in the air, and if you controlled the shot
carefully it will split at the absolute highest point in the shot's arc, when it has
almost 0 upward or downward momentum. The shot then spreads out
into a nice vertical line of bubbles, and all 6 bubbles rain down onto the
target's head for full damage.
<p>
The reason this works so nicely is because the shot had no vertical
momentum at the moment it split open. The less momentum the SS has
when it opens, the smaller the spread is. The way it spreads is to fan out
in the direction the shot is moving... so if it's moving downward, it fans
out horizontally (which is bad). If it's moving forward but has no downward
momentum, it spreads out vertically (which is good). This might 
make it clear:
<p>
<img src="badssopening.jpg"><img src="goodssopening.jpg">
<p>
To ensure that the shot opens at its peak, you must figure out
how much power to use. The more power you shoot with,
the longer the shot is in the air. The correct power, on average,
is about 3.8 bars. For close shots (1/2 screen or less) you'd use
about 3.7. For far shots (near 1 screen) you'd use 3.9. The
maximum power is full, and the maximum range is about 1.4
screens (past that it spreads out too much, and your damage 
will only be low 300's... it'd be better to just use shot 2).
I've heard that with helping wind (upwind and sometimes 
tailwind) it's possible to hit even further way with the skybomb.
For example Chill claims that in 10 upwind, 66 with full power 
can hit 2 screens away. I still need to confirm this but the 
theory is that normally these far shots would need more 
than your maximum power, but upwind reduces the power
back to something that you're allowed to shoot with. You'd
think the shot spreads too much to be useful, but the trick 
to it is to allow the shot to open in a diagonal line that 
closely matches the falling angle of your shot. Then 
the balls all somehow slide diagonally into them. In such 
situations the diagonal line happens when the shot opens 
a bit early, on the way up.
<p>
Power controls the airtime of this shot, which means to control
the distance of the shot you must choose the correct angle.
This is somewhat tricky. In the past, Turtle could fire a test shot
using shot 1 or shot 2... and if the test shot hit the target 
then you knew your SS also would hit.
<p>
Then Softnyx changed things... they made it so that
after the SS opens, the weight of the shot changes and all the
balls fall to earth more quickly than before. That means that
the SS sort of changes weight in the middle of the shot.
It's the only Gunbound shot that does that. It also means
that instead of the shot moving in a perfect parabola, the path
of the shot looks a bit lopsided. Since shot 1 and shot 2 move
with normal, predictable paths, you cannot tell where the SS
is going to land based on what shot 1 or shot 2 did.
You will have to memorize the distances and angle for the
SS as if it were a completely different shot.
<p>
<img src="shotpathss.jpg"><img src="skybombdifference.jpg">
<p>
Distance is controlled by your angle, which can be hard to measure. 
1 screen is split into 14 parts/angles, and it's not evenly split.
There's no simple trick to divide a screen into 14 parts, 
but the average distance between angles is about  56 pixels. 
I think that distance increases a little bit as you shoot further,
so an 89 shot is 55 pixels from 90, but a 76 shot is ~60 
pixels from 77. This is a nearly full power shot, and like full power, 
it's possible for an enemy to be sitting between 2 
different angles, where one angle lands a bit past them, but 
the next higher angle lands a bit in front of them. An enemy at this 
distance is in a "blindspot" and the only way to get a nice skybomb 
on them is to walk turtle forward or backward. You can't really 
change your power in these situations because it gives the shot 
bad airtime and your damage will be lower (300's instead of 500-600).
<p>
Here's the info you need to know to hit with the skybomb.
Sorry if any of these numbers are incorrect. Because the change
to skybomb weight is recent, good information isn't available.
I recommend memorizing a few basic angles shown in the table
below. At least memorize half and 1 screen shots.
Then move on to my simplified windchart. After you're confident
with both of these, try out the link to Mishigne's skybomb
guide at the bottom of this page. This chart was perfect for the 
old skybomb (like all of Mishi's info) but it may not work as well 
for the new one with the the changed weight.
<p>
