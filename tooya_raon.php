Advanced Lightning Raon Slip (performed by Ybom, creater?)<br>
<br>
This is a rare breed of shot. Following all the guidelines of the Roan slip (read above to make sure you know all the rules), your shot can penetrait through larger chunks of land. Shoot your mines (dual even) through a streak of lightning (or use the item yourself), and the mines will go through the land. The lightning bolt will hit the enemy, cause some slight land damage, and make the land small enough for the raon baby to glitch its way through.<br>
