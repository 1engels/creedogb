Shot 2/SS Trick shots:<br>
<br>
Some of these I am taking Directly from Bloodbugs Guide. Be weary these glitches were not discovered by me.Raon's mines are not like normal shots, they do not do land damage. Knowing this is crucial....<br>
<br>
Raon Slip (by Bloodbug)<br>
<br>
The Raon slip is a technique where you shoot mines at thin layers of land. The screen shot really describes it all. You shotgun under them, as the babies hit the land they actually grow through the land, and impact upon the enemy. If you decide to dual your enemy will most likely fall from the few pixels they are standing on, HOWEVER(!), as soon as your 2nd set of mines touches any land, they will warp back to where the enemy was standing (due to a sweet little glitch). <br>
<br>
<img src="tooya2.jpg"><img src="tooya3.jpg"><br>
<br>
There are 2 exceptions for this glitch, if you do not notice them then your shot will fail.<br>
<br>
Do not dual with Thor on: when your 2nd set of mines touches land, instead of warping back, thor shoots a laser at there landing spot, and that is where they are processed.<br>
<br>
The shot Must touch land! If you dual full power at an enemy above you with an angle of 45, Say bye bye to your 300 dmg. If your 2nd set of mines does not touch any land, then it will not warp back.<br>
<br>
Raon Bounce (by Bloodbug)<br>
<br>
This is almost the same shot, however you are shooting at thick land this time, and your raon shots bounce off the ceiling (or wall). When I say bounce, I actually mean they fall straight down. So the trick here is to aim right above your enemy, and the shot will fall down straight on his head.<br>
<br>
