Putting it all to use<br>
Ideally you should be playing on Miramo as it has nice slopes all around and beautiful weather. IMO Grub depends fully on weather effects, without them you're screwed. 8in1 with Thor/Force racks up a decent 450-600 damage, more than enough to help a friend out.<br>
<p>
Lightning on the other hand can either be devestating or lemony. It's by far the best for Grub if you hit 8in1 right in the center of thier mobile, this will deal 500+ damage and put them in a 2 inch pit, wasting at least 1 of thier turns. If you can hit an armour through lightning you can pretty much say gg. If it doesn't hit the center, however, it won't deal too much damage and will create a pit beside them, doing more harm than good.<br>
