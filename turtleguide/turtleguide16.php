<h1>Banpao (3 bar) formula</h1>
<h1>Windcharts & notes</h1>
<hr>
<blockquote>
Here are the 3 windcharts you need. You actually
can almost get away with memorizing just one. The
differences between the windcharts are fairly small.
And you can plan on not using banpao at half screen
or closer, because at those distances 2.4 is usually
much safer and easier to use, and won't need such a high
angle. To start out, try to memorize the banpao 1 screen
chart, then move on to the 1.5 screen chart. Later, if you
still think you need it, learn the half screen chart.
<p>
If you're not bored enough to memorize charts, you can try
using the general 2.4 windchart with banpao. Just remember
that banpao requires a little more angle change than 2.4
because of the longer airtime (for example, in situations where
you'd raise 8 angles with 2.4 you might raise 9 with banpao).
<p>
Please note that for far shots, opposite wind holds
your shot back too much to use the 'usual' wind adjustment.
You must change your angle and also add a bit of power.
For the original knat windcharts (which aren't perfect but
have more detail than my charts) go here:
