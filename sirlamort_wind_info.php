SirLaMort’s Precision Aiming Guide – With Wind Charts<br>
<br>
Before I start, I would like to warn anyone who thinks custom rulers
are<br> cheating to stop reading. This guide is for people who want good
enough accuracy that they can decide what part of the enemy to hit,<br>
front, back, middle… etc.<br>
<br>
Section 1- Your zero wind game:<br>
The first step to become good at aiming is your 0 wind game.<br>
The only times I miss in 0 wind are when I do not have angle,<br>
or my keyboard or mouse messes up. I use the 2.99Bar, or 3Bar<br>
power for most my shots. I do not use the part system.<br>
<br>
To make a custom ruler for 3Bar power is a bit of work.<br>
First you must find 0 wind games. I prefer shooting at 3Bar<br>
power at every two angles, and taking a screenshot when the shot is<br>
exactly horizontal with the back of the arrow. I do not use F11,<br>
I use print screen. It is much faster! After you have screenshots of<br>
Turtle/Armors shot at every two angles. You can then take them and<br>
put them as your pc’s desktop background, so they are the same size<br>
as in Gunbound.<br>
<br>
Now you have your measurements, but where do you put them?<br>
On a custom ruler! Very thick paper is preferable, cut out a<br>
12x 3/4th strip of paper and there you go! This method of<br>
getting measurements via screenshots of 95% accurate, but<br>
because slice is somewhat micro-erratic, it is not perfect.<br>
I personally check the important angles a few times, Ex:
85,80,75,70.<br>
<br>
Section 2 – Your wind game:<br>
I hope everyone knows the basic universal formula:<br>
wind factor x wind -+ angle.<br>
Wind factor you get from your wind chart, wind… if you don’t know
where<br> to find that one, please stop reading. Angle is your distance
away in 0 wind, with your shooting system.<br>
<br>
I am pretty good at wind factor judgment, but you can use some<br>
visual aids like mentioned on the wind chart. The thing to remember<br>
is wind factors are DIFFERENT for every single angle in banpao.<br>
So you must use common sense if the angle you have to use is
different<br> from a wind chart.
<br>
If one of my wind factors ever fails you, there is an easy<br>
system to find the right wind factor. If you miss by one angle,<br>
change your wind factor until your calculator has the angle<br>
that you are sure would have hit, then try again. Yes I<br>
prefer calculator, trying to multiply .33x26 just isn’t my thing…<br>
<br>
Section 3 – using Feel with my wind charts.<br>
There is a small application for these wind charts even if<br>
your not using formula. I prefer to play a lot of games just with<br>
feel, so relaxing. But if you must hit, there’s always formula.<br>
<br>
Say for example that your doing high angle and you know the<br>
correct measurements, and you miss by half an angle. The wind is<br>
toward you, the wind goes up one, and you think “should hit now
right?”.<br> Lets look closely and say the wind factor is .39. You can’t
depend on<br>.39 of an angle closer to get you a good hit, so you must change power.<br

Section 4 – General Rules of Power Adjustment<br>
There is always a time when your calculator tells you some stupid<br>
angle like: 75.56 Your only way of hitting is to either adjust<br>
power or move. If you use angle 75, you overshoot, if you use 76<br>
you undershoot.<br>
<br>
<p>Power change rules, the higher the wind the more effect:
For wind >> and up, slight power change = big distance change.<br>
For wind << and down, slight power change = small distance change.<br>
<br>
Distance rules,<br>
Close to enemy, slight power change = slight distance change.<br>
Far from enemy, slight power change = big distance change.
