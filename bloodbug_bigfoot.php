2. Nasty Bigfoot Tricks<br>
<br>
I won't explain the bases of bigfoot like bunging, angle flattening or raping. I'm sure you will find by yourself.<br>
...<br>
Ok, maybe not. Here's some quick explications.<br>
<br>
Bunging = Shoot at little less than at the feet of your target.<br>
Flattening = Done long range. Just shoot, and you'll demolish any angle the opponent first had.<br>
Raping = Done the same way as bunging.<br>
