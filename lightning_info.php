<br>
If you can control whether your shot will hit the center, front, or back of the enemy, then try to aim to hit a little bit on the right side of the enemy (when using shot 2)... always, no matter what side you are shooting from. This will get best damage. <br>
<br>
If you are in a game with dual+, you probably will do better with dual+ than dual, and you shouldn't use dual much with lightning unless there is thor. It is not as useful with other weathers.<br>
<br>
The only time to use the SS is when you cannot hit the enemy easily any other way, or if you know it will ruin their angle (Because they are on the front of a tall hill or they are in a deep pit) or when you can hit 2 enemies at the same time for double damage. Otherwise you do very crappy damage and the SS is a waste.<br>
