<h1>Shotgunning and Turtlekaze</h1>
<hr>
<blockquote>
This is the easiest shot - point your 'aimer' directly at the enemy and use full
power.  I recommend using dragshot shooting mode for this, it takes less time 
than slice. You can switch back to slice afterwards if you want.
Unlike a lot of bots, for Turtle it's important to use full power because
the shot starts spreading apart as soon as it leaves your cannon. The more
power you use, the further the shot goes before it splits.
