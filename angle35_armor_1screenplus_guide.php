Formula <br>
<p>
Power to Adjust <br>
<p>
(delta)F’ = F/F0 * (Fw * i) /100 <br>
<p>
If you pay more attention, you will see that the power needed for ¼ <br>
screen shooting is half of the power needed for 1 screen shooting, i.e.<br>
1.24 / 2.48 = 0.5. <br>
<p>
Anyway, here are some data that you need, i.e. Distance factor = F/F0 <br>
<p>
Distance Factor <br>
¼ 0.500 <br>
½ 0.706 <br>
¾ 0.867 <br>
1 (F0) 1.000 <br>
1.25 1.169 <br>
1.5 1.222 <br>
<p>
This distance factor is actually the air time value. That’s why you <br>
will be using it in F and F’. Because when an object travels for a <br>
longer air time, more affection the wind will be. <br>
<p>
Power to shoot <br>
<p>
F’ = F + ?F’ <br>
<p>
Nothing much to explain here. Just apply the formula by summing wind 0 <br>
power with the power for wind compensation. <br>
<p>
Where <br>
<p>
F = Power under wind 0 condition <br>
F0 = Power for 1 screen under wind 0 condition <br>
(delta)F’ = Power to adjust <br>
F’ = Power to shoot <br>
Fw = Power of Wind <br>
i = Wind index <br>
<p>
<p>
Example <br>
<p>
Target distance = ¼ of 1 full screen <br>
Fw = 24 (Against you) <br>
<p>
Solution: <br>
<p>
i = 0.96 <br>
F = 1.24 <br>
(delta)F’ = 0.5 * 24 * 0.96/100 = 11.52/100 ~= 0.12 <br>
F’ = 1.24 + 0.12 = 1.36 <br>
