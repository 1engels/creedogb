<h1>Trickshots</h1><br>
<h3>Some out of the ordinary stunts you can try with boomer</h3>
<hr>
<blockquote>
<ul>
<li>Reverse hooks - These are sort of like ugly backshots that you allow
to intentionally hook. You can use them with 3 bars of power or a bit more.
It's not the same as using a 3 bar backshot. The difference between the
backshot and the reverse hook is sort of like the difference between a
normal 3 bar lob shot, and a 3 bar shotgun. One moves in a smooth
curving arc, the other in a sharp straight line. Anyway, to do a reverse
hook, face either direction (I like doing them while facing my enemy)
and use angles around 75. That's 75 pointing away from the target.
Then use 3 bars or a bit more power, and the shot will fly up, stop,
then hook towards the enemy. It's the same thing as trying a hook
at a close range enemy when the wind is too strong. The hook
flies backward. You can do this intentionally with experience.
A simple measurement to start with is angle 80 = ~1/4 screen with
a reverse hook, and maybe 77 for 1/3 screen. The range is short.
Also notice it goes the opposite way that a backshot does:
for a backshot, a higher angle usually means the shot lands further.
For reverse hooks, higher angles bring the shot closer.

<li>Super stunty backshots - if the wind is 20+, you can do full power backshots 
that move in a smooth curve instead of hooking.  There's a formula for judging
these: Use HongHong's backshot formula for determining the TK angle.  Now
mentally divide the screen into 20 parts.  You will raise your angle by
the number of distance units (20 being 1 full screen) between you and the 
enemy.  Lastly, just fire with full power.  If the wind is strong enough and
you judged the distance correctly, you can make precise stunt shots this way.
An alternate method that I find easier: Calculate the needed angle for a 2.5
bar backshot using the HongHong system, then lower 2 angles and fire
with full power (Credit to Sayo for showing me this). In very upward wind
you might need to lower more than 2 angles, in wind that isn't so vertical
you might need to lower only 1. But the 2 angle rule is a good place to
start.

<li>Timebomb SS - by using very low power, around 1 bar of power, you can get an
SS to open just in front of the enemy's face, which looks neat.  It also has
some practical uses - you can use the lowest angle possible to connect with
the SS, which means getting true angle for your SS is simple.  You can also
aim the SS very precisely at close range.  The difference between an OK hit 
and a perfect hit with boomer's SS is about 100 hit points.  So being able
to aim it very carefully is useful.  
The easiest way to judge your angle when aiming the 1 bar SS: Measure the
distance between you and the enemy in parts, each part is about 5 angles.
Each part is about the same size as the parts used to measure a 2.25 lob
(i.e. 15 parts to a half screen distance).  
So to determine the angle, measure how many parts are between you and the 
enemy, then multiply it by 5.   Subtract the result from 90.  That will be
the angle you need (at 1 bar of power) to hit with your SS.  Note that
for very close shots, you should probably use angle 80 or so, not 85.
You don't want to risk damaging yourself.  Also be aware that height 
differences require a small power adjustment, i.e. use 1.1 bars if the 
enemy is slightly above you.  For hitting enemies beyond the angle 50
mark, you should also start to add power.
<a href="1barss.jpg" target=about:blank>Visual Aid</a>

<li>Booty shot - this goofy trick can be done with anyone.  Charge the power for 
any shot using slice, then juuust before you release the spacebar, turn and face 
the wrong direction almost at the same time as you let go.  You'll face the 
wrong way but still shoot correctly, boomer appears to shoot from the rear.
</ul>
