<h1>2.4 formula - Part 3</h1>
<h1>'Funny' Wind (examples)</h1>
<hr>
<blockquote>
If you're still reading you either understand or you're really really bored.
Maybe both. Here are examples of how I adjust my shot when dealing with
funny wind. It's not always right, but it works for me and is fast and easy.
<p>
<b>Step 1: Estimate whether the wind angle matches your shooting angle<br>
or if it mirrors your shooting angle.<br>
Step 2: Adjust angle if necessary.<br>
Step 3: Adjust power if necessary.
</b>
<p>
<img src="funnyexample1.gif">
<p>
Target is at half screen.
<p>
For wind that is up+against like this, I adjust like most other
shots, meaning I change angle only. If the wind's direction
were very close to straight up, I'd adjust power, but this is
actually pointing against me quite a bit (straight up wind
would be pointing at 90 degrees, and this is pointing at
63 degrees, so this wind doesn't match or mirror my shooting
angle, which would be 75 in 0 wind).
<p>
How much do I lower my angle? Well, I try to use the nearest
wind factor to help me out. The up+back wind factor is .35,
which would mean I need to adjust almost 6 angles. This wind is
a bit above that wind factor, so it's not as 'against' you. So
I will adjust less, I'll adjust 4 angles only.
<p>
<p>
<img src="funnyexample2.gif">
<p>
Target is at 2/3 screen.
<p>
I see this wind as a combination of downward and opposite wind. It's
almost perfectly straight down, but if you look closely it's also a little
bit against you.
<p>
To make the shot travel far enough and have enough airtime, you just
have to adjust for the 'down' part of the wind using the usual rule: Add .1 bars
for every 7 wind. My mental shortcut is this: I know that in 21 wind I'd add .3 bars.
Since this is 20 wind, I know I just need to add a hair under .3 bars.
<p>
That would make my shot travel the right distance if the wind were perfectly
down, but it's also a little against you, so the shot will be held back a bit.
To compensate for the 'against' part of the wind, I will lower my angle
in addition to the power change. The angle change will be small. In these
situations I might lower a maximum of 4 angles. Since this is pretty close
to being straight down wind, I will lower only 2 angles.
<p>
My final answer therefore is to lower 2 angles (from 70 to 68) and add about .3 bars
(from 2.4 bars of power to 2.7).
<p>
<img src="funnyexample3.gif">
<p>
Target is a bit past 2/3 screen.
<p> 
The first thing I automatically do in all situations is estimate my basic shooting
angle in 0 wind. If 2/3 screen is angle 70, then a bit past would be maybe angle
68. Now I look at this wind and immediately ask myself if the wind angle might
match my shooting angle. You might remember that halfway between up+forward
(wind angle 45) and straight up (wind angle 90) is wind angle 66. This current
wind direction is a little bit higher than that halfway point. 
You can tell by looking at the blue highlighted part at the edge of the ring.
If this were angle 66, that blue part would line up perfectly with the small
orange marks showing up & up+forward wind (and also down & down+back wind).
The blue part is a little higher than those marks, so the wind angle is a little
higher than 66. It's probably 68.
<p>
With wind like this, where the wind angle matches your shooting angle, you can
easily make the shot with just a power adjustment. The trick is that you must reduce 
power more than usual: Lower power by .1 every 5 wind instead of .1 every 7 wind,
and you should hit in most cases.
<p>
<img src="funnyexample4.gif">
<p>
Target is at 1 screen.
<p> 
I can see right away this wind doesn't match or mirror my shooting angle.
This wind might mirror shooting angle 75 or so, but for a 1 screen distance shot
I'd use angle 60. So I plan on changing angle and power.
<p>
Since wind is mostly up, I will reduce using the .1-every-7 rule, therefore my
shooting power is 2.3 instead of 2.5 bars.
<p>
Since it's also a little against me, I will lower my angle by 2 angles. If it were up+back
I'd lower 3 angles, since this is more vertical I adjust less.
<p>
<img src="funnyexample5.gif">
<p>
One last example. Target is at 5/6 screen.
<p>
This is difficult wind to deal with. Right away, when I see that wind is 20 or more
and pointing downward, I assume I must add power. If I don't, the shot might not
have enough airtime to converge. So I use the rule and plan on adding about .3 bars.
<p>
This is very near the kind of wind that nullifies angle 63 shots, although it's hard to
see that. So merely adding power without changing angle would be enough to
make this shot fly too far when shooting at angle 63. For this distance I use 65.
The shot will still fly far at angle 65. So I need to raise my angle.
This is mostly a matter of experience and feel but I would probably add 4 degrees.
So my final shot would be 69 (65+4), 2.75 (2.45 + .3).
</blockquote>

