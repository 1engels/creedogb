<p>
If this chart seems too complex for you to learn, here are some shortcuts:<br>
- Memorize only the angle wind factors, don't bother with the power until later.<br>
Then you can use a test shot or feel to hit them once you're sure you have the right angle.<br>
- Don't memorize the small 'in-between' factors until you master the 9 basic factors.<br>
- This doesn't work for me, but it is also possible to memorize the power factors,<br>
then guess the angle and try a test shot. Based on how your test shot lands,<br>
you can then adjust angle as needed while using the same power.
