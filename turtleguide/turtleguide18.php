<h1>Other high angle formulas:</h1>
<h1>3.4 shooting method</h1>
<hr>
<blockquote>
This is not a common formula, I think I'm the only one who uses it. I don't use it very much
because I prefer to use banpao where possible. I use this instead of the full power formula
because it's a bit less power, and a little easier to control. If you need to, you can add power
to your shot. The formula also has easy to use measurements, you can divide the screen
into thirds, which is possible with the 2.4 system.
<p>
The basic idea behind 3.4 is that for any given angle, your shot will go exactly twice as far
as a 2.4 bar shot. So, angle 75 travels 1 full screen instead of half screen. Angle 80 travels
2/3 screen instead of 1/3. And angle 60 goes 2 full screens. At this power level you can
make calculated shots at very long ranges (further than banpao) and measuring is easy.
<p>
This formula does have some of the drawbacks of full power: The distance between angles is
large enough that an enemy can be between 2 angles (forcing you to change your power).
The effect of the wind is huge, although the banpao windchart works pretty well with this formula.
And you will still need to get a pretty high angle to use 3.4. In most situations, I would use
banpao instead of this formula. Learn it for fun if you want, or just skip to the next section.
