2.4 power shooting method<br>
<br>
With the 2.4 method, you use 2.4 bars of power, and change angle. The screen is divided into 30 parts (we can all do that in our heads, right?) and you adjust your angle. Use 90, as always, to hit yourself. 1/4 screen is angle 83, half screen is 75, 3/4 screen is 67, full screen is 60. Also note that after 1/2 screen you will likely need to use about 2.5 bars power.<br>
Recap:<br>
1/4 - 83<br>
2/4 - 75<br>
3/4 - 67<br>
Full - 60<br>
<br>
I don't like the 2.4 method because of 4 main reasons. 1) limited use. In 0 wind, it only covers 1 screen distance, so what happens when you have a full screen shot with 22 wind blowing against you? That's right, you don't. 2) Different power. The whole point of using a concentrated power method is to not have to change power, but what's this? You change power as you shoot farther! I don't understand it. 3) There method was designed for the armormobile. While the shot weights are pretty much the same, there are differences with how the wind effects it, and your shots are often off. 4) WHO THE HECK CHOOSES 2.4 POWER? I can see maybe 2 bars, or even 2 and a half, but 2.4? Eesh, no thanks.. On the plus side, it is very accurate when you are close and in low wind, and it's the closest to shooting ss in the timebomb form (though you usually won't get good results :X) Also, this method can be seen as having no blind spots (aside from the huge ones on the side that you can't get to, but we'll ignore them for that :o)<br>
