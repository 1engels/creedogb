<h1>Long Forks</h1>
<hr>
<blockquote>
First, I would like to thank Mishigne for showing me that forks 
are possible. I think he was the first Turtle I ever saw use long forks.
Most of the info I use originally came from him, and some from
Rem4rk and Chill. I can't share all of it but I want to thank these three 
players for what little I know.
<p>
A long fork is a shot 2 that comes together at the moment it 
hits the enemy. You get full damage without keeping 
the shot in the air 2.5 seconds. A fork's airtime is similar to 
the timebomb SS, but it's a little shorter. 
It's probably 2 seconds or maybe a hair less.
<p>
<img src="beamfork.jpg">
<p>
This is different from a shot 2 that barely comes together after about
2.3 seconds. That's called a fake fork (or spoon). That's just a normal 
shot 2.
<p>
<img src="fakefork.jpg">
<p>
Like the SS, the secret to forks is lots of work and memorization.
Calculating forks is a lot of work and is mostly done to show off.
You almost never HAVE to fork unless you're on very flat land and 
just can't get a decent shooting angle. 
So forks are for players who already have a pretty good 
idea how to do Timebomb SSes. I would not recommend learning forks
until you can do everything else in this guide. 
<p>
Most of the fork info I'm going to show is based on hitting your fork with 
the final (fifth) crossing of shot 2. It's also possible to hit with earlier spins but mostly 
your shot will be easiest if you use the final spin. I give some extra 
info anyway if have the time to memorize the other forks.
<p>
Calculating forks works like calculating Timebomb SS:<br>
<b>1. Find the 0 wind angle and power.<br>
2. Adjust for elevation.<br>
3. Adjust for wind.<br>
4. Shoot.<br>
</b>
<p>
To get the 0 wind angle and power, just look to see what landmarks the enemy 
is closest to. As before, I have info for a 6 part split and an 8 part split. Because
forks are very precise and very sensitive to power, I will be giving exact powers 
rather than rounding up or down. Try to be as close as you can in power.
