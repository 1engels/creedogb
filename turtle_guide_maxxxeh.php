When to ss, and why<br>
<br>
Turtle SS is great, but it isnat always applicable.<br>
<br>
You should not SS when-<br>
<br>
-The enemy has ~600 life left, and you can dual. A dual with generally lead to higher damages than an SS, even whe the SS is perfect.<br>
<br>
-The SS won't open. This should be obvious, but when you're playing in SS death, and a turtle is in melee/shotgun range, and shooting ss's that just plop on the enemy, it is so unbeleivably frustrating that it makes you not want to play turtle. Ever.<br>
<br>
-You are unsure of you shot. if you're less than 60% sure that the shot will hit right then a test shot wouldn't hurt. This is the only one that doesnt really matter, because you could be about 1% on the ss working, and it could still work. however, if you miss or something, it'd just be a waste of delay.<br>
<br>
-There is a tornado around the enemy. You may think this might help the balls get pulled toward the enemy, but it can have the direct opposite effect. See image.<br>
<img src="maxxxeh16.jpg"><br>
The nado may reflect the shot if it opens at the edge of it. That one in particular pissed me off because it went perfectly through the other tornado, got to him, opened, and went back the wrong way. Ugh. Oh, and its even more fun when you have it set up great, and it reflects it back and you either tk or hit yourself. So yeah, dont SS the nado.<br>
<br>
<br>
You should SS when-<br>
<br>
-The enemy is sitting in a force. I mean, the force is going straight through them, so that when the SS opens it will have the force. Done correctly, this will net 700+ damage. A dual when the person is in the force will get 550-600. You would only want to dual over SS when the 2 shot 2's are still intertwining and going through the force, thus spending more time it it. It really doesnt matter, but it may be if the game is on the line.<br>
<br>
-When thor is active, ss is better than a dual because it will call 8 beams of thor, while a dual only calls 4. They will generally get the same amount of damage, but SS will be less delay.<br>
<br>
-The Enemy is in/near a lightning. If you do a skybomb SS and land it on top of an enemy after passing it through a lightning, boy are they screwed. Not only do they take 600+ damage, but they are in a tiny little hole that they have to waste 2 turns getting out of. Fun.<br>
