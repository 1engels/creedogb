<h1>Other high angle formulas:</h1>
<h1>Full power (4 bar) shots</h1>
<hr>
<blockquote>
There are 2 other high angle formulas I like to use with Turtle.
The first is full power, which is usually the first 'formula' beginners learn.
It's usually called "high angling".
Full power is not a shooting method I would recommend except
to show off. It has several problems.
<ul>
<li> The distance between angles is huge, so sometimes you must
use less power to hit an enemy who is between 2 angles.
<li> Maximum power means max airtime which = maximum effect from
the wind. Every tiny wind change affects full power shots a lot.
<li> Shooting with such high power means you need a very high angle
to start with, often in the 80's. Getting such angles isn't always possible.
<li> The distance between angles changes a lot depending on the wind.
Being "1 angle off" in 0 wind might mean you missed by 70 pixels, but being
one angle off in strong tailwind might mean you missed by 90 pixels.
<li> The number of parts/angles per screen is an irregular number, 11.
Most other shooting formulas allow you to split the screen into fourths
or thirds but with full you cannot.
<li>You cannot add power to compensate for a shot that misses
barely in front of the enemy.
</ul>
If you still want to play with full power, the basic shots to remember are:<br>
<b>1 screen = 79 full</b><br>
<b>half screen = 84 'unfull'</b>
<p>
By 'unfull' I mean about 3.8 bars of power. People who use full power 
must get used to occasionally switching to 3.8 bars to hit in between 2 angles.
If you used 84 full for half screen, it would fly too far. If you tried 85, it would
come up short. So the only solution is 84 with 3.8 power. Almost any useful landmark
will require less than full to hit with Turtle: 1/4 screen needs 87 unfull, 1/3 screen
needs 86 unfull, 3/4 screen needs 81 unfull, etc. So full power pretty much blows.
<p>
It can be useful in some situations. If you're deep in a pit, with a big wall of land in front of you,
you need to use a very high angle to shoot out without hitting land. You may need to use
near full power anyway to hit with this kind of angle. It's also handy when an enemy is
deep inside a pit and has land in front of them. The only way to hit them without
drilling through the land in front of them is to have your shot drop down on them vertically.
Full power means the shot comes to earth with the most vertical angle possible.
<p>
Here is a chart showing <a href="../turtlehighanglechart.jpg">
Turtle's full power distances</a> and some measuring tricks.
<p>
You might also want to check out this <a href="../armour.4.0.pow.0.wind.jpg">
old armor high angle chart</a>. It gives distances in exact pixels and shows shots that go past one
screen. Armor's shots are the exact same weight as Turtle's so this info works for Turtle too.
A quick note: For some reason the author started the 2nd screen information in a weird spot.
So the places where you see the purple lines are not where the 2nd screen shots will land, unless
you drag the left edge of your screen to the spot where angle 80 full would normally land.
<p>
High angle tips:<br>
<ul>
<li>Use drag to get to full power quickly, unless you need to use 3.8 bars. Then it can be
difficult to drag without accidentally going to full. I slice for 3.8 bars.
<li>Full is good for very long shots past 1.5 screens, where even banpao could not hit.
Remember that the distances between angles gets smaller when you're shooting far away.
What that means is that even though half screen is '5.5 angles' normally, the distance from
1.0 to 1.5 screens is a full 6 angles. So 1.5 screens is angle 73 full (79 for 1 screen, -6 for
the next half screen). 2 screens is 67 full, not 68 as you might expect.
<li>when you miss a tiny bit in front or back, get used to walking Turtle forward or backward
to compensate. It's much easier than changing power.
<li>Also use the walk trick when the wind changes by 1, for example if you hit them in
15 opposite wind, and on your next turn it's 16 wind, just walk forward. You can't add power
anyway, and trying to lower 1 angle and use 3.8 is a pain in the ass. For most formulas, 1
wind difference doesn't matter but for this formula it's enough to cause a miss (of course 
if the wind = 1 then you can shoot as if it's 0).
<li>There's no real windchart for full power because it'd be too difficult to create one.
Wind affects the shot so much that it'd be hard to make an accurate chart. As a starting point,
I adjust the shot the same way I would using the banpao windchart, then I usually adjust
1 angle more (so in 14 tailwind I might adjust 9 angles instead of 8). The important thing is
to get a good idea of what '1 angle' looks like, and judge from your missed shots whether
you are 1 angle off, 2 angles off, or if you missed by some fractional angle. Once
you think you have a lock on the enemy you must watch wind and their position carefully.
<li>The only good news I have about full power is that elevation doesn't matter much. 
All shots require thinking about elevation, but this gives you the most room for error. 
But you should remember that the shot doesn't literally come straight down onto their 
heads, it comes in at an angle. So if an enemy is below you, you must aim a bit 
in front of their body, and if they're above you you will aim a bit past their body. 
