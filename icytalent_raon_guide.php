<br>
I donít have a screenshot of a shotgun, but it is easy to do anyway, and a dual can do as high as 700+ damage =)<br>
Other than using shotgun, I believe there is only one other way to make the 2 mines explode on contact, which is to use angle 89, or possibly angle 88. Here is a screenshot where I used a dual+ with shot 2 first, but the mines exploded before my shot 1 hit.<br>
<br>
<img src="icytalentraon4.jpg"></img><br>
<br>
Lastly, shot 2 can be used to boost the damage of your teammate. If you make the mines land on the target but doesnít explode, your teammate can shoot at the same target to deal extra damage. For example, a nak shot 2 with thor on a target with 2 mines on him will cause 500+ damage =)<br>
<br>
<br>
