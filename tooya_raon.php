Raon is a very nifty bot, most consider it a weak bunge bot... that is because they do not know its full potential. Raon is a deadly machine, that, if used properly, can kill a mammoth in 2 shots. It has a decent angle, decent climbing rate, but its movement is utter crap.<br>
