<li>How do you make a formula? Where did you get this info?
<p>
Some info I got from friends, some I got from 1v1 testing and experience, and some I calculated 
using publicly available programs. Forks and timebombs can be calculated with Yoyobuae's 
<a href=http://www.gbgl-hq.com/forums/index.php?topic=7359.0>Aimchart</a> which gives you 
the angles and powers for any fixed timing shot. You can use it to get accurate wind factors 
for almost any shot, once you know the airtime of that shot.
<ul>
<li>Generate a chart with the correct airtime (like ~1.9 seconds for forks)
<li>Open the chart in photoshop and rotate it so it's flat
<li>Copy and paste the wind scale onto a new layer
<li>Rotate the windscale to the correct angle to figure out wind adjustment for a 
certain wind factor. Like rotate it 45 degrees and set the "0" wind point at the
1/2 screen position on the chart to get info about 45 degree up+back wind at 
the half screen mark (the wind scale 'points' in the direction opposite 
the wind that you're charting).
<li>Look up different angles and powers for this wind, like maybe look up the angles 
and powers for 5 wind, 10 wind, 15 wind, etc. For my half screen Turtle ss chart I 
looked up the info for every 3 wind.
<li>Write down how the angle and and power changed from your 0 wind shot.
<li>Divide the angle adjustment by the wind's strength to get a wind factor.<br>
Divide the power adjustment by the wind's strength to get a power factor.
<li>Average several factors together to get one "overall" factor, or try
averaging several weak factors to get a number for weak wind adjusting,
then several medium factors to get medium adjusting, etc.
<li>Write this info on your chart then repeat for at least 8 other wind directions.
Most formulas are easier and don't require this kind of effort to calculate. 
Timebombs and forks are special because they are fixed timing shots. You must 
not only try to get the distance correct, you must get the airtime correct.
Most shots don't care at all about airtime, and you only need to figure out how 
to get the distance correct. 
</ul>

