What this means is that if your test shot for 2 does decent damage and comes 
together, then your SS will fail (too early or too late). The angle and power to make 
a good Turtle SS will cause a very weak shot 2 that mostly misses and splashes the
enemy for a little damage only.
<li>With the above info in mind, it's possible to convert a successful timebomb into a 
good shot 2. The trick is simply to raise 3 angles and add a bit of power. I'm not sure 
if this works with long SS shots near 1 screen, and it definitely won't work with angles
below 45.
<p>
