<br>
SS<br>
<br>
Raon SS is weak, but it is used as a tool to kill a target without much skill. If an enemy is quite far away, and if there is no obstacles on the land, a Raon SS can walk towards the enemy even if you are not sure of the power needed.<br>
<br>
In solo games, sometimes you need to help your teammate to kill a dangerous foe but you are unsure of the angle and power. A Raon SS can come in handy here.<br>
<br>
IMPORTANT: Raon SS is lighter than shot 1 and shot 2, make sure you keep this in mind.<br>
<br>
Raon SS is difficult to use if the enemy is on a pillar or pixel with no land surrounding him. You have to be very precise to make the SS explode on contact.<br>
If the enemy is in a hole, it is very easy to hit him with SS.<br>
