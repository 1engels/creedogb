 A lot of people have landmarks for halfscreen such as 70 2.0, etc, numbers around that.<br>
<br>
Given the full power 1 screen angle, you can pretty accurately calculate what this shot will be -<br>
<br>
sin(-1)(2sin(2A))/2=the new angle, at 2.0 power instead of 4.0 power<br>
<br>
So, for example, cake is 81 full screen, so sin(162)=.3090,<br>
sin(-1)(.6180)=38.2 or 141.8<br>
angle 19 or 71 at 2 power will give you exactly halfscreen, provided that the fullscreen measurement is correct.<br>
<br>
It also corrects the 70, 2 power measurement to a more accurate 71 :)<br>
<br>
Turtle is 79 full screen, sin(158)=.374<br>
sin(-1)(.748)=48.5 or 131.5<br>
so angle 24 or 66 at 2 power will give halfscreen.<br>
<br>
Of course you won't be calculating this in the heat of battle, so just calculate beforehand >.><br>
