<body>Boomer Angle 90 non-formula<br>
<br>
For the longest time I wondered what this formula was, then I realized everyone who talked about "angle 90 formula" was just full of crap :D ... there is no formula for angle 90 because you'd need to memorize a fixed power first, then you'd need to know how every type of wind affects your shot at 90. Without a specific power level it's very hard to make an accurate wind chart for your fixed angle 90, and even if you have a specific power level it's hard - because the closer you come to angle 90 the less predictable your shot seems to be. Most windcharts are estimates of how a shot will behave when shot between ... let's say 60-80 degrees. These windcharts aren't meant for shooting straight up (which is why almost all fixed power shots seem to land short of where you expect when you calculate that you need angle 90 or close to it).<br>
<br>
OK, I said a whole lot without giving any info, so here's how to use 90:<br>
<br>
1. Know a LOT of fixed power shots and basic wind adjustment for them. Memorize how to adjust full power shots, 2 bar shots, banpao, 2.8, 70 bjsl(?), 2.25 bar shots, etc.<br>
2. With this knowledge, choose a power level based on one of these shots and estimate how far your shot will travel using angle 90 and the chosen power level. Estimating this is easy:<br>
First: Multiply the wind strength by the wind factor.<br>
Second: The result is how many screen distance units your shot should travel. I know that needs some explanation. Let's say you know that with a fixed power 2.25 bar shot, you would raise 10 angles in 9 wind. Another way of saying that is that 9 wind carries your shot 10 "parts". Since 2.25 is based on a 30 part screen, you know 9 wind is carrying 10/30 parts. Therefore 9 wind carries your shot 1/3 screen distance. So 90, 2.25 should travel about 1/3 screen.<br>
3. Make small adjustments based on wind direction, enemy distance, and the fact that angle 90 shots seem to land shorter than you expect. This is mostly feel/experience/guesswork.<br>
<br>
Let's say wind is 10 down+towards the enemy. From experience you know that at full power your shot is carried something like 6 parts in this kind of wind (I dunno for sure). Therefore since 1 screen is 9 parts, you know that 90 full power should travel 2/3 of a screen.<br>
<br>
You get the idea? You need lots of experience with adjusting angle for wind using other formulas, then you can start to get a good accurate picture of how much power to use from angle 90. Try to estimate where 2 bars, 2.25, 2.8, or full power might go then adjust power based on those guesses to make accurate hits.<br>
