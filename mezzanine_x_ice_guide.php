About Defense Lowering:<br>
Ice is the only bot in gunbound that can lower the defense of your enemy. When you do any damage to an enemy with shot 2 or SS, their defense is lowered. A small negative number with (DEF) next to it will appear above the enemy's health, to the right side. This number signifies how much defense has been lowered. The minimum defense that a mobile can have is -50. When an enemy tags in tag game mode, the 2nd bot will not have negative defense. However, if he changes back to the original bot, the negative defense it had will be remembered and unchanged. In score mode, the defense is kept the entire game, even after the enemy lands after dieing. Because of this, Ice has the biggest advantage in Score mode.<br>
