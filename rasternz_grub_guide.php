Shot 2<br>
The heart of Grub, what makes it. How does it work though? How do you get the most damage? Simple..<br>
<p>
The trick to grub 4in1 is loosely angle 70 BJSL, that is to say you use a base angle of 70 in 0 wind and adjust the angle to account for any other wind.<br>
Creedo's wind chart comes in handy for angle adjustment. Word of warning however, the closer the wind gets to being vertical (up or down) , the 4in1 gets less likely if you adjust angle only. You're better off adjusting power in this wind, anyway, here's Creedo's chart.<br>
