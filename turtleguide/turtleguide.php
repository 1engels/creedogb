<blockquote>
I think Turtle is one of the best bots, if played well. It's got high attack
strength and the best defense in the game, and it's pretty flexible.
In a way it's like armor, but a little harder to use. Turtle's major weakness
is its poor movement and climbing ability. Or, you could say its weakness is
needing a good angle to shoot from, something you can't always get because
of its bad mobility. Its delay is just 'average' so it doesn't have the delay
problems Armor has, but it can lose delay to good Trico and Boomer players.
The SS is also quite hard to use and cannot hit in all situations,
but I think it's the most satisfying, fun shot in the game.
<p>
Some Turtle history: about  3+ years ago, the angle range was placed
lower and a bit in front of Turtle's body. Turtle could
get a nice low shotgun angle even from flat ground.
So Turtle was a very nooby shotgunning bot. To make things worse,
you could push Turtle's "gun" deep into the enemy's body and shotgun for
huge (400+) damage, while doing very little damage to yourself.
The Softnyx solution was to nerf Turtle by moving the angle range higher up, so
that shotgunning without a downward angle became impossible. They also moved
the shooting point far back inside Turtle's body, so that the shot emerged from
its center. Now doing shotguns right inside the enemy's body will
cause a lot of damage to yourself. This forced people to play
Turtle with a little more skill, and it stopped being popular for a while.
I think that it became popular and 'fashionable' again in part because of
the guides of dedicated Turtle fans like Ramza and myself.
Or maybe that had nothing to do with it, maybe it's because Turtle hasn't changed
much while many other bots have been weakened and changed over time.
</blockquote>

