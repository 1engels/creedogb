<h1>Angles below 45</h1>
<hr>
<blockquote>
In Gunbound physics (as in the real world) a shot will travel at its 
maximum distance when you shoot with angle 45, for any given power. 
So a 2.4 shot will travel further angle 45 than it would at 40, or 50, etc.
Interestingly, the angles above 45 and below 45 sort of mirror each other 
in terms of distance... a shot 10 angles over 45 will travel the same 
distance as a shot 10 angles under 45.
<p>
When you try advanced Turtle stuff like forks past half screen or timeboms 
past 1 screen, you will use angles under 45 a lot. Some of the rules 
that you're used to working with are backwards when you use angles 
under 45.
<p>
Let's say you're doing a test shot for a timebomb, or a fork, and the 
airtime is incorrect... but you hit the enemy anyway. Your angle is 
under 45.
<p>
If you need more airtime, you must raise angle and decrease power.<br>
If you raised angle only you'd increase the airtime, but it'd fly too far.<br>
If you need less angle you lower angle and increase power.<br>
It's like turning a low angle lob into a shotgun. A shotgun travels faster.<br>
<p>
Let's say your shot had good airtime but it missed the enemy.
<p>
If the shot missed in back of them, you must lower angle.<br>
Think of shots under 45 as shotguns. You're no longer making shots 
that miss 'in front of' or 'behind' enemies, you're now thinking in terms the shot
flying above or below the target. If your shot went behind them, you aimed too 
high, so you lower angle. If you hit the dirt in front them, you aimed too low, so 
you raise angle.
<p>
Wind adjustment is different under 45 too. A low angle shot will more easily 
cut through tailwind and opposite wind. But upward wind and downward wind 
have a stronger effect. Think of the path of your shot as a 
sheet in the wind. The more of the sheet that you expose to the wind, 
the more strongly it can be blown around. 
<p>
<img src="45windeffect.jpg">
<p>
Some familiar wind factors act differently when shooting under 45. 
The Turtle 1 screen timebomb windchart is very nearly an angle 45 
windchart. For angles under 45 - <br>
<ul>
<li>up+back wind still requires lots of angle reduction, but the power 
won't change much. In fact, with greater wind you may be adding power.
That's because you're shooting with such a drastically lower angle, so the shot 
crashes into the ground sooner, and therefore the airtime is shortened
too much. So you must add power to keep the shot airborne long enough.
<li>down+forward wind also requires lots of angle change, in the other 
direction. Again the power doesn't change much, and you may 
end up reducing a bit if you change it at all.
<li>down+back wind also makes the shot crash into the ground sooner,
and again the fix is to simply add power. This used to work like opposite 
wind, but now it's more like downward wind, which means to get enough 
airtime, you might need to raise your angle. Not by very much, for example 
when doing an angle 38, 1.5 screen SS... in maximum down+back wind 
you may need to raise only 1 angle.
<li>up+forward wind will nearly match the shooting angle. When over 45, 
this wind acts like tailwind more than like upwind. So raising angle is 
needed. At angle 45 exactly, no angle change is needed. So below 45, 
it's backwards, it's more like upwind than tailwind. So you actually need 
to lower your angle in this wind. Since this is the opposite of the wind 
listed above, you can expect it to work in a similar way... the angle change 
is very small, 1 or 2 degrees only.
</blockquote>

