Basic Information<br>
<br>
Shot 1<br>
Delay ÅE750<br>
Damage ÅE100+ to 180+<br>
<br>
Shot 1 is a very weak shot normally, but its strength lies in its ability to bunge and screw the opponentís angle. It is also used as a test shot, a delay-beating shot, or an angle making shot. However, the power of this shot is increased greatly when weather effects like thor, lightning and force steps in.<br>
<br>
Later on in this guide, other uses of shot 1 will be explained.<br>
<br>
<br>
Shot 2<br>
Delay ÅE900<br>
Damage ÅE250+ - 300+!!<br>
<br>
Shot 2 is the main weapon of Raon. It can be very deadly, or can be just a tool to make you lose. This shot creates 2 Raon mines which will not always cause damage on contact. Instead, the mines will land on the ground, and when there are players close by, the indicator on the minesÅEhead will turn red, which means it will move towards the target on your next turn. If the indicator on the minesÅEhead is green, that means it will stay in the spot, There are many ways to play with this shot, which will be explained later on.<br>
<br>
<br>
SS<br>
Delay ÅE1300<br>
Damage ÅE200+ - 300+<br>
<br>
Raon has a weak SS, but that doesnít mean it is useless. Raonís SS has a unique feature which can be very beneficial or very annoying. Once u fire this shot, a big Raon toy will start walking in the direction of your shot, provided that the land isnít too steep for it to walk over, and it will explode upon contact on the first target it meets. A target is not just a player, it can be another Raon mine or just a dead mobile. If there is nothing in its path, it will continue walking and explode after about 5 to 6 seconds.<br>
