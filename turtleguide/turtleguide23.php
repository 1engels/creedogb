<h2>Wind adjustment</h2>
By now you should be able to read these charts. They're similar to the
skybomb charts, they have both angle and power adjustment.
The large numbers are your angle wind factors. For the half screen chart Blue = raise angle,
and blue is also used for reduced powers.
Red = lower angle, and is also used for increased power. The 1 screen chart is similar but it's Orange 
(decrease angle, or added power), and Green (increase angle or reduced power).
<p>
I made a simplified version of the charts which you might find easier to use than the
more traditional chart used for the skybomb SS. The math is easier, but may be 
a little less accurate. But in almost all situations it will be accurate enough 
to make a decent timebomb.
The original charts are linked for those who want exact numbers. 
They include the smaller numbers for "in between" factors 
that I didn't bother making power levels for... because it would cause a lot 
of clutter and you can estimate the power anyway if you know the rest of the chart.
<p>
The line dividing the two colors is where you might not need to change 
your angle at all, you'd only change power. There's also a faint line showing the 
wind direction where your power would be the same as 0 wind, you'd change only angle.
<p>
The bold, italic text shows your power change factors. These numbers are difficult 
to work with if you're trying to calculate only in your head so I 
gave simplified rules for them.
Example: .02 = adjust .2 bars every 10 wind. So in 20 wind adjust .4 bars of power.
<p>
Original chart: <a href="../half_sd_timebomb_windchart.jpg">Click here</a>.
<p>
<img src="../half_sd_timebomb_windchart_simple.jpg">
<p>
Example shot:
Wind is 45 degrees (blowing up+forward) and wind strength is 20.
The chart's factors for this 1/5 of wind (.2 every 11).
<p>
Therefore, raise angle by (1/5th of 20) which is about 4.
You're also going to have to lower power. The rule is (.2 every 11) so 
if it were 11 wind, you'd lower .2 bars exactly... if it were 22 wind, 
you'd lower .4 bars. Since this is a bit less than 22 wind, you will 
lower a bit less than .4 bars. Call it .35 bars.
So - your basic half screen angle is 66, 1.97
Your angle change is 4, so now you will shoot at 70.
Your power change is .35, so the new power is 1.62.
Angle 70, 1.62 will make a nice timebomb in this wind.
<p>
The chart isn't perfect for distances outside of half screen, 
but it will be very close. You can almost use it for 1/4 screen 
and 3/4 screen and have good results, there are only small 
differences in the wind factors and power changes. If you learn this chart 
and want to try using the SS at other distances, some basic rules of thumb are:
<p>
Closer than half screen: Generally all winds have a little stronger effect 
on the shot's distance, so you might need to lower 1 more angle 
for opposite wind and raise 1 more angle for tailwind. Also up+back 
and down+forward wind have an even stronger effect, these wind directions 
might require 2 angles more adjustment in some cases.
<p>
Between half and 3/4 screen: The half screen chart is mostly accurate, 
but you adjust your angle less than before. For example, in 20 wind blowing 
against you... you might adjust 10 angles for a half screen shot but only 9 
angles for a 2/3 shot, and 8 for a 3/4 shot. Other wind factors decrease 
slightly as well. You might notice the windchart has a lot of factors near .5 
...when shooting 2/3 to 3/4 screen, try using .4 factors in place of these .5's 
and you'll probably get the correct angle.<br>
Note: The exception to the rule when shooting further is that straight up and
