<h1>HOW TO ACTUALLY USE BOOMER</h1>
<hr>
<blockquote>
Boomer has several different 'shooting modes', and you must be able to switch at 
any time between one and the next.  You can't just pick an angle and use it all 
the time with boomer, because wind will force you to change it, and judging the 
correct power to use with one angle would be too difficult when you see how much 
wind affects your shot.  I will list the shooting methods I use - keep in mind
that you don't have to master all of these methods and formulas to be a good 
player, many players use a lot of feel to make nice shots with boomer. Some
recommend that you just stick to low angles in place of all formula lobs or
hooks. If you had to play boomer using only one method all the time, I'd
say you should go with a low angle like 35 or so.
<p>
<h2>Boomer shooting modes</h2>
<br>
<ul>
<li>Shotgun - you aim directly at the enemy and cut loose at max power.
<li>'Normal' shot - use a fixed angle like 35, use feeling/memorized shots to find power level.
<li>Lob/fixed power shot - in 0/low wind, always shoot with the same power and 
<br>use a formula to find the angle you need to hit them.
<li>Backshot - allow your shot to blow backward to hit them, increases dmg/gold.
<li>High angle shot - similar to lob, but you always use max power.  Best when
wind is 0 or low.  You can use formulas to find the right angle.
<li>Hook - also a formula shot, use max power against the wind.  Shot travels in 
an inverted "V" shaped arc.  Guess the angle based on wind strength/direction.   
</ul>
<p>
<h2>Your shooting plan:</h2>
<ul>
<li>Backshot if there's a fairly easy/safe backshot available, because it's 
about 20% more damage than usual, and also more gold.  If you're playing 
serious, remember: any true angle shot is better than a weak angle backshot.
<li>Shotgun whenever you can't backshot, as long as you can get true angle.  
Also shotgun if you aren't confident the backshot will hit.
<li>If you can't backshot or shotgun anyone, follow these rules:
</ul>
<p>
<h2>WIND TOWARDS THE ENEMY:</h2>
<ul>
<li>Within one screen use lob or high angle if the wind is weak and you think you
can easily judge what angle you need to use to hit them.
<li>If wind is kind of strong, a normal shot is safest. 
<li>If you can't get high angle for a lob, a normal shot with the lowest angle
you can get is usually safest.
</ul>
<p>
<h2>WIND AGAINST YOU:</h2>
<ul>
<li>If wind is gently against you, use a hook. Remember: in strongish winds, a hook 
can blow backwards, and the closer the enemy is, the more likely it blows back.
<li>If wind is strong and you fear 'blowback', use a normal shot, angle 35 is good.
<li>Also if you cannot get true angle to make a hook or the enemy is very close, 
use a normal against-the-wind shot, don't try to get cute with hooks.
</ul>
</blockquote>

