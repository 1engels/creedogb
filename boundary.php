<body>Boomer "Boundary" formula.<br>
<br>
This isn't really a formula, because there's no sure way to know the exact distance your shot will travel. You feel/guess it. But it's fun and looks cool and is sorta hard to do. I like it because it's the most dangerous formula in gunbound. I'll explain soon.<br>
<br>
The wind is blowing against you, so<br>
(you) X_____ <---- wind_____X (enemy)<br>
<br>
Step 1: Find your "tk angle" based on the honghong backshot formula.<br>
Step 2: Lower 1 or 2 angles more (so in other words, the highest angle you can use without causing a backshot).<br>
Step 3: Guess power to hit your enemy. It looks really cool at closer ranges. For long ranges, it doesn't seem very cool (because at 1 screen range you'll be using full power to hit the enemy, so it's just a boring old full power hook). For strong winds it also doesn't look cool (because you're using a very low angle like 35 and everyone knows low angle against the wind shots with boomer). So the BEST wind for this is weak wind, especially wind that is blowing downwards quite a bit.<br>
<br>
Some basic info to help you make your shot *but no guarantees*:<br>
1/3 screen = ~2 bars<br>
1/2 screen = ~2.8 bars (yes nearly 3 bars just to travel half screen)<br>
1 screen = ~4 bars.<br>
<br>
Why these numbers work: The formula for a 1 SD hook is just 60-wind adjustment.<br>
So if you're doing some angle between 50 and 60 for a full screen, and you know full power will go 1 screen about, then half a screen will be 2.8 bars (using the magic formula I posted recently).<br>
<br>
You can make cute small SSes at very close range when the wind is against by using this formula. It might even allow you to do SSes that a full power hook would not, because your shot travels upwards much more slowly with this formula than it would at full power. So you can try an SS with only 1/3 of the screen to work with.<br>
For your reference: <a href="backshotformula.htm">Backshot formula (how to find tk angle)</a><br>
<br>
Oh, why I call it the most dangerous formula in gunbound: When you use the boundary angle between backshot and hook, sometimes you miscalculate and the shot backshots. It almost always flies back directly in your face and TKs you, which is quite humorous for the enemy but makes your teammates a bit irritated after the 10th time you do it. Even if you calculate the angle correctly, remember that a shot flies back based on both angle and the amount of power you put into it. For example at 2.5 bars the shot might hook, but at 2 bars with the same angle and wind, it's a backshot. So there's always a little bit of guessing and luck involved if you want to use this non-formula.<br><br>
