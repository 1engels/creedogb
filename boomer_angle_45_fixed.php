<body>Angle 45 - the formula is easy, always use 45 and alter power<br>
based on wind. This is only for 0 wind or wind towards the enemy.<br>
Your shot must change completely when wind is against you.<br>
<br>
Angle 45 guidelines:<br>
<br>
1/2 screen: 1.45 bars<br>
3/4 screen: 1.75 bars<br>
1.0 screen: 2.15 bars<br>
<br>
<img src="boomer_fix45_1screen.jpg"><br>
Wind adjustment:<br>
<br>
UP: lower power .1 for every 5 wind. E.G. 20 wind up, lower .4 bars.<br>
	a 1 screen shot goes from 2.15 to 2.75.<br>
<br>
DOWN: add power .1 for every 6 wind. E.G. 25 wind down, add .4 bars.<br>
	a 1 screen shot goes from 2.15 to 2.55.<br>
<br>
TOWARDS AND UP: reduce .1 power for every 3 wind, and then<br>
reduce 1 or 2 mm more power, E.G. 10 wind up+forward, lower .4 bars.<br>
	a 1 screen shot goes from 2.15 to 1.75.<br>
<br>
TOWARDS AND DOWN: Almost no change. Reduce .1 for 7+ wind. Reduce .2<br>
bars for 17+ wind. E.G. 12 wind down+forward, lower .1 bars.<br>
	a 1 screen shot goes from 2.15 to 2.04.<br>
<br>
DIRECTLY TOWARDS ENEMY: reduce .1 power for every 8 wind<br>
E.G. 16 wind forward, lower .2 bars.<br>
	a 1 screen shot goes from 2.15 to 1.95.<br>
<br>
<img src="boomer_fix45_wind.jpg"><br>
<br>
Other info:<br>
<br>
1. pixels count when measuring power.<br>
1 mm of power is 5 pixels, or .05 bars. it's the smallest amount<br>
that you can change your power when using slice. Two of this amount<br>
equals .1 bars. There are 20 mm to 1 bar. <br>
So if you know you must adjust .2, that's 4 of the smallest<br>
amount you can control using slice. Also, if it helps to visualize it:<br>
<br>
.2 = 1/5th of a bar<br>
.35 = 1/3rd of a bar<br>
.5 = half of a bar<br>
.65 = 2/3rds of a bar<br>
.8 = 4/5ths of a bar<br>
<br>
2. I recommend slice for power control usually.<br>
In some cases, drag may be better both for speed and accuracy -<br>
you can drag to an exact pixel and sometimes a few pixels of power<br>
(like 3 instead of 5) are needed to make the shot hit properly.<br>
Most of the time slice is good enough. The key thing about fixed<br>
angle formulas is to develop a feel with them, and I don't think<br>
you can feel a dragshot.<br>
<br>
3. When shooting half screen shots, the amount of adjustment won't<br>
always be as large as the adjustment for a 1 screen shot.<br>
The wind adjustment for 1/2 and 1 screen are close but not the same.<br>
<br>
4. Watch for elevation, a small requires some careful changes in power.<br>
It can help to visualize the path of the shot, imagine shooting a bit<br>
past an enemy who is higher than you, try to see if their body<br>
would 'block' a particular shot like a 1 screen shot. Alternarely, for<br>
lower enemies, imagine something like a half screen shot and see if<br>
the enemy lies at a 45 degree angle (roughly) to the place where<br>
your visualized shot would pass you and go below your ground level.<br>
<br>
5. Remember to play smart: watch wind, ditch the formula is there's<br>
an easier shot or a different formula you're sure of.<br><br>
