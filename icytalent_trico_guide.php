before u see the charts....<br>
MUST READ THIS - MANUAL ON USING THE WIND CHART<br>
the red numbers are the wind factors, used for calculating the angles for compensation, for example, if it states +0.5, it means u adjust the angle by (plus wind X factor)<br>
<br>
the numbers below the red ones are the adjustments for power<br>
for example, if it says 6W+0.1, it means u add 0.1 power for EVERY 6 wind, which means if the wind is 6, u add 0.1, if the wind is 12, u add 0.2 and so on.<br>
