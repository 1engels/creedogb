<body><h1>Boomer 2.5 fixed power</h1><br>
Credit to "ImnotLisa" for telling me the basic angles.<br>
I think she changed her nickname now so don't bother her<br>
about it.<br>
<br>
This is just about all I know for it:<br>
<br>
79 2.5 = half screen<br>
66 2.5 = full screen<br>
<br>
I'm a bit blur on the half screen angle, it might be 78.<br>
<br>
Wind adjustment: since this is just for fun I don't think<br>
you should try it in any serious wind. <br>
<br>
Direct (left or right) wind:<br>
For weak winds under 5, raise your angle by wind-1 angles.<br>
For wind over 5, don't subtract 1 angle, just raise by wind*1.<br>
For wind over 8, raise by wind*1, then 1 more angle.<br>
At this point you'll be very close to 90 if you want to hit half<br>
screen, and you should probably try an easier formula.<br>
<br>
Wind diagonal down+towards the enemy (45 degrees down):<br>
I raise my angle by wind/2 degrees. It seems to work ok.<br>
<br>
Wind diagonal up+towards the enemy (45 degrees up):<br>
Weak winds less than 5, raise by wind * 1 angles.<br>
Wind over 5, do the same but raise 1 more angle.<br>
Wind over 7, same but raise 2 more angles.<br>
<br>
Basic example: <br>
<br>
Enemy is a bit past half screen. Wind is 4 directly<br>
towards them. Your angle would be 79 for<br>
half screen, so for a bit past use 78 and 2.5 bars.<br>
To adjust for wind, raise 3 angles. So you use 82, 2.5.<br>
<br>
Enemy is 1 screen distance. Wind is 9 up+towards (45 deg. up).<br>
Your angle would be 66 for 1 screen, to adjust for wind you raise<br>
by wind * 1 degrees then add 2 more angles. So 9 * 1 (+2) = 11.<br>
Your angle goes from 66 to 77. 77, 2.5 bars should hit 1 screen<br>
distance. <br>
<br>
I think for winds that point upward, it is safer to reduce a bit of<br>
power even after you raise your angle, i.e. just 2.4 bars is ok.<br>
<br>
I hope people will try this and tell me if it works, I haven't<br>
used it very much and can't confirm wind adjustment etc.<br>
Again it's just for fun - use it because nobody else knows it.<br><br>
