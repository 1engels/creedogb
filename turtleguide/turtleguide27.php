<p> 
Other fun forks:<br>
56, 1.1 (hits 1/6 SD) - this is good when you're just a bit too far for 1 spin or<br>
even 2 spin forks. I find it's a very easy and flexible fork for distances like this.<br>
10, 3.95 (hits 1 SD) - a 1 screen stunt shot.<br>
30, 3.15 (hits 1.5 SD) - Very stunty long fork.<br>
23 3.95 (hits 2 SD) - WTF :D
<p>
Elevation adjustment for forks is difficult because so much depends on whether 
you're shooting under angle 45 or over 45. The good news is that in most cases, 
you can be one angle off and sometimes even 2 angles off and still 
make a decent fork.
<p>
What I'd recommend doing with forks is to use the timebomb elevation adjustment,
but adjust a little bit more for forks. Maybe 1 or 2 angles more. I can't be more precise 
than that because it's too much work, but if someone 
wants to calculate elevation and send me the info I'll be glad to share it.
<p>
