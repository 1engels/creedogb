Introduction:<br>
Ice is a very basic seeming mobile. The shots don't hook, they do not spin, refuse to call down other forces, or even walk around. The only complication of ice is that its firing points are different. Shot 1 comes up high from the cannon, shot 2 comes from below, out of the mammoth's trunk. Ice has the highest health in the game, but deals low damage. Its delay isn't impressive without using items. However, Ice has the ability to lower the defense of enemies. This is very important.<br>
