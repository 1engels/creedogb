<h1>2.4 formula - Part 2</h1>
<h1>Wind adjustment</h1>
<hr>
<blockquote>
The previous section told you how to choose your angle
when the wind is 0. This will tell you how to choose angle when
there's wind. In different winds, the formula is still mostly about
choosing the correct angle. For most shots, the power
will remain 2.4. In certain kinds of wind, it will be necessary
to adjust your power also. First, here is the windchart.
It has 8 simple numbers to remember. You must
memorize all of them if you want to be able to hit nice
shots in all winds. It's ok if you don't understand them
right now, just memorize the numbers if you can, or keep
reading and try to memorize later.
<p>
<img src="windchart2b.jpg">
<p>
Wind adjustment steps:
<p>
<b>
1. Figure out your 0 wind shooting angle.<br>
2. Find the number on the chart that matches the current wind's direction<br>
3. Multiply the wind's strength by that number (which is called the 'wind factor')<br>
4. Raise or lower your angle by the result.<br>
5. Shoot with the appropriate power level (usually 2.4 bars)
</b>
<p>
For step 1, measure the distance to your target and figure out
the angle you'd normally use in 0 wind. For example you'd use angle
70 for a 2/3 screen distance shot. Remember that angle
in your head while you go through the next steps.
<p>
Step 2, look to see which way the wind's blowing and look at
that direction on the windchart. There's a small number.
That number is the 'wind factor' for that wind direction.
<p>
Step 3, multiply wind strength (the number in the blue wind circle)
by the wind factor you chose in step 2. Your answer will be some small
number between 1 and 16.
<p>
Step 4, raise or lower your angle by the number you got in step 3.
On the chart, blue numbers are for wind directions that require you to
raise your angle, and red numbers mean you must lower your angle.
Let's say the wind factor you used in step 2 was blue, and your answer
from step 3 was "8". You would raise your angle 8 degrees.
So the example angle of 70 would be raised to 78.
<p>
Step 5 is the easiest. For most of your shots, you will use 2.4 bars.
As usual, if the enemy is close to you, use only 2.35... and for far
shots you may need 2.45 or 2.5 bars. These small adjustments
always must be made whether there's wind or not.
<p>
The hard part is dealing with winds that are very vertical (nearly
straight up or straight down). It is best to adjust your power when
dealing for those winds. For example, if the wind's pointing straight
down I will just use my 0 wind angle and add power. How much power?
A good rule is this: add about .1 bars for every 7 wind strength.
So a half screen shot in 14 wind pointing straight down... would be
angle 75 (my 0 wind angle) and 2.6 bars (2.4 bars, with .2 bars added).
For wind pointing up, you reduce instead of adding, but the rule is the same. 
Also, if you aren't confused yet... the longer your shot is, 
the more upwind and downwind will alter it. So remember when shooting
far that you must add or reduce a little more than what the ".1 every 7 wind"
rule says.
<p>
By making these power adjustments, you make your shot fly more
like a 'normal' shot in 0 wind. If you tried adjusting angle without
adjusting power, the distances between angles would become strange - 
in upward wind these distances would get larger, and in downward wind
they'd get smaller. This can make your life hard when you miss. For example,
normally if you miss in front of an enemy by 1/10th of a screen, you'd
lower 3 angles for your next shot. In 0 wind, that would get you a guaranteed
hit. But if the wind is pointing upward and is strong, lowering 3 angles might
make it fly too far. That's part of the reason why you change your power using
the ".1 every 7 wind" guideline.
<p>
The other big reason is that in downward wind, your 2.4 bar shot will not
stay in the air for the minimum 2.5 seconds needed to guarantee that the
shot closes. If you get in the habit of adding power in downward wind,
then your shot will get the extra airtime it needs.
<p>
If staring at the chart doesn't do much for you, don't worry, I give
examples next page.
</blockquote>

