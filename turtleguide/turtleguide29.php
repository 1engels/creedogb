<h1>Frequently Asked Questions</h1>
<hr>
<blockquote>
Here are some questions I get asked a lot, in some way or another.<br>
Some are about Turtle, and some are more general.
<ul>
<li>Is it really necessary to deal with all these numbers? I don't want to
shoot with formulas, I'm a feel player. Can't I play Turtle with feel?
<p>
Yes, you can play with feel if you want, but feel often won't get you the correct 
angle for SSes or forks, which require very precise angle and power.
For shot 2's, feel works up to a point, but you have to remember that 
you need lots of airtime for this shot. Feeling a low angle shot is easy 
and natural, but feeling high angle, high power shots is not so easy. 
For example if you can feel shots pretty well with 
angle 60, your shot 2 will have enough airtime for 3/4 and 1 screen 
shots, but not enough airtime for 1/2 screen. Even with angle 70, the
airtime is barely enough for a half screen shot, and you'll be forced to 
use angles in the 80's for closer shots. Also, I believe that you cannot 
feel shots past 1 screen... you need experience to make those 
shots, or else a good formula. My style of play is to calculate as much 
as I can, unless there's no time or it's important to shoot fast. Remember
though that formulas don't guarantee hits or make you an aimbot. A LOT 
of the shots I mention (including timebombs) suggest that you learn to 
feel your shots. Don't use formulas in a dumb way without understanding 
how the shot works, how the formula is compensating for wind, etc.

<li>What about BJSL or some of the other formulas?
<p>
BJSL is a great formula for most bots, but Turtle is suited to a fixed power 
shot because you cannot use low power for close shots (which is part of how 
BJSL works). People will already say there are too many formulas in this 
guide so I won't cover BJSL or other shooting methods.

<li>How do I hit middle range shots if I can't get a high angle?
<p>
The safest way, if you can get the angle, is to use the "ss angle+3" trick.
Figure out the angle for an SS, then add 3 angles and use a bit more power.
This gives the shot the 2.5 second airtime needed to converge, barely. For
a half screen shot you'd need an angle like 69 to pull this off. If you 
can't even get a nice angle like 69, then you should memorize the fork shots 
listed earlier. They're great when you're forced to shoot with a low angle.

<li>I don't understand the windchart.
<p>
When I first started using the windchart, I used a very simple method:<br>
Wind against or down: lower angle by (wind/2) then shoot.<br>
Wind up or towards: raise angle by (wind/2) then shoot.<br>
Basic example: 10 wind against you, do 10/2 to get 5. Lower 
your angle by 5. So a half screen shot goes from 75, 2.4 to 70 2.4...<br>
Wind 14 towards enemy: Raise angle by 14/2, so
raise 7 angles, then shoot with the usual power.
Later I figured out that wind/2 is not good enough for all situations, and I 
would remember special exceptions like "medium wind, add or lower 1 more
angle, strong wind add or lower 2 more angles"... so for example, in 14 wind towards 
the enemy, raising 7 angles would make the shot fly too far because this is "medium" 
wind, so I should have raised 1 more angle (8 angles). Then I figured out 
that for other wind directions, I should divide by other numbers, like down+forward wind 
needs wind/4. Finally, I came to understand windcharts using decimals instead of 
fractions. "Wind/2" is the same thing as saying ".5 of wind". So a wind factor of .5 meant
wind/2, a factor of .25 meant wind/4, etc. Most windcharts use factors like .5, .3, .25, etc.
The way I am able to use these charts is to convert these fractions into some shortcut,
like converting .35 into "Wind/3". That way I could see that 21 wind x .35 is roughly 7.
Doing this is easier for me than doing decimal multiplication.<br>
If you're still confused, it may help to read <a href="../using_formula.php">
this info</a>. If you still don't understand, I probably can't help you because I 
have explained this the best I can, in the easiest words I can.

