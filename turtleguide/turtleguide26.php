<li>SS is most powerful with lightning. Both skybombs and timebombs benefit 
greatly from it. The SS doesn't get much help from thor and seemingly no help from
force, unless the force is right on top of the enemy. But with lightning SS damage 
jumps nicely (because you get a hit for every ball in the SS). A very good SS might
do 600 for a tight timebomb vs Boomer, 630 vs. shield type bots, and 650 for a perfectly 
tight skybomb that hits dead center. With lightning, you can get 850+ damage, although 
less than 700 is more typical. There's another very nice benefit: The pit made from 
the lightning often screws the enemy's angle badly. In the last pic I dropped sate 
to the lower level of stardust with it.
<p>
<img src="ss_angled.jpg">
<p>
<li>Use the SS when an enemy has almost no life left and you're not sure how to 
hit them. By intentionally making a wide spread with the SS, you can cover a large 
area and almost guarantee that you're able to hit something. It's a waste to SS 
this way except to kill though.
