Turtle is a mobile with Close to if not the best defense in the game, along with very high attack power (250 average shot 2). It has low movement and poor bunging. People often criticize it for its small angle range, however, I rarely have a problem with it.<br>
<br>
Also, be sure to note that normal mobiles have +10 delay for each second that they wait to shoot, Thus, if you take 20 seconds to shoot, you have +200 delay added to your shot. However, Turtle gets + 12 delay added to each second, not +10. That makes +220 delay added to your shot if you take all 20 seconds. It makes sense, since turtle is slow, right? (Since all of gunbound makes sense, with trico shooting cabage and grub shooting tiny electric balls. psmily.gif). This is a good thing to note, because if you're trying to outdelay someone it can really screw you up if you're expecting less delay.<br>
