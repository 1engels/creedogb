=================<br>
Shooting methods<br>
=================<br>
<br>
There are pretty much 3 ways of shooting turtle, though I would only reccomend two, and I myself try to use only one. Here they are in order of preferance. But first, some info.<br>
<br>
How to measure how far away they are.<br>
-I use the screen to measure how far away people are when shooting. When you right click, you can hold the screen in place. After doing that, place yourself on the edge of the screen and look at where the enemy sits. 1/4 screen is the "all" or "team" button. 1/2 screen can be measured two ways; either from the edge to the wind meter/gauge, but also from the "all" button to the end of the power meter. 3/4 screen is from the edge to the end of the power meter. Full screen is obviously from edge to edge.<br>
<br>
When shooting, the power bar is marked by markers that divide it into 4 bars. I refer to this, so you need to know what I mean.<br>
<br>
Full power Method<br>
<br>
With this shooting method you use full power and change angle. This is opposite to how most people learn (most lower power when the shot is long, add power when its short). To hit yourself, use angle 90, someone halfscreen will be angle 85, and someone a full screen distance will be angle 79. 1/4 screen would be half of half screen distance, or 2.5 angles less. But you can't do that, so drop power instead. 1/4 screen is about 87 3 3/4 bars. 3/4 screen is angle 81, 3 3/4..<br>
<br>
To recap,<br>
1/4 - 87 3 & 3/4 power<br>
2/4 - 85 full power will land just short, so 84 and 3&34 will hit. Thanks Creedo.<br>
3/4 - 81 3 & 3/4 power<br>
Full - 79 full power<br>
<br>
I do not like this method because it is inaccurate. The point is to be angle to use full power all the time, but you will often find you need to change your power because the enemy is inbetween full power shots, or in your blind spot. Blind spots are bad. Also, you will find it very hard to consistently get a high enough angle to do this. On the plus side, you can usually get off a good SS after knowing the full power spots. Also, this is your farthest shot, so it is sometimes your only option. :)<br>
<br>
Full power is usable, though. You can't see me, but when lined up, he was outside my screen by aboput an angle, so I launch an angle 78 full power dual, and land it on the grub's head. The damage seems weak for a force shot on a grub, though. Not sure why. Maybe I'm just spoiled on the ones that get 700+.<br>
