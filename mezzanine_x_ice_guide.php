WINNING THE DELAY GAME:<br>
<br>
Using delay well is the #1 important Pro skill to learn!<br>
<br>
You should learn Drag Mode of shooting.<br>
-It is much faster then slice for 2.2 and full power shots<br>
-You have finer control over your power, instead of going by tenths, the bar can stop anywhere<br>
<br>
In order to switch to drag, hit ESC in a game, and click DRAG and hit OK. Drag uses your mouse in stead of the spacebar.<br>
<br>
You click your mouse and drag it to the right slowly or quickly to raise your power. You can go quickly, then slow down to get exactly the power you want very quickly. Every second spent is 10 more delay! After practice, an excellent player can take their shot in 0-1 seconds.<br>
<br>
Both Attacking and Defending ice tactics depend on using very little delay in order to in against an enemy. However close these two tactics are to foolproof, good delay usage is a key factor to winning. Delay is listed in the bottom left corner of screen ... wait. Creedo wrote something on delay. AH here it is.<br>
