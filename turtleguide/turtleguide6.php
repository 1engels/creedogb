<h1>Short Fork</h1>  
<hr>
<blockquote>
This is a short-range shot 2 that 'spins' only 1 time before
hitting the enemy. So the shot leaves your cannon, spreads
out, then comes back together and hits them.
Some players think this is a difficult shot that needs
some magic angle and power. It's not like that. It's a very simple
short shot that can be done with almost random
angle and power. The only thing you need to be careful of is that
your target isn't too far away, and that your angle is high enough
(but not too high). To aim this shot, I don't use a fixed
angle, I just point my red aimer a certain distance above the
enemy's 'eyeball' (i.e. the top of their mobile). Then I feel
the power, which usually will be about .6 or .7 bars. In this
picture you can see a red line showing about how far to
aim above the enemy's 'eyes'.
<p>
<img src="shortfork.jpg">
<p>
I use the same method even when the enemy is below me,
which can be a difficult place to fork because the bottom half of
the shot wants to hit the land in front of them while the top half
wants to fly past their heads. Just try to visualize the
correct distance between your aimer and the top of their mobile.
<p>
<img src="lowanglefork.jpg">
<p>
It's useful to be able to fork when the enemy is above you too.
There are 2 different ways for a fork to succeed depending
on how far above you the enemy is. If they're a little bit above
you, the fork works normally and each half spreads out once
before hitting them.
<p>
<img src="highanglefork.jpg">
<p>
If they're far above you, it's possible
to aim the shot so that the bottom half of the fork spreads out
once and hits, but the upper half bends out, misses, bends
out a second time, then hits. So the bottom half did one 'spin' but
the upper half does two. Ramza calls these slugshots, and they're
very useful. I'd rather try a slugshot (which also looks cool) than
a high angle shot when I'm trapped in a pit and the enemy is
far above me. In these cases, you cannot really aim your
pointer the same height above the enemy's head. You still
aim to shoot a bit above their eye and just feel the power.
For most forks, it seems useful to aim to make the shot hit
the enemy's back a bit, i.e. you overpower the shot a hair.
For this fork, you aim as if you wanted to lob a shot 1
directly onto their head.
<p>
Sometimes the enemy is too far away or too high above
you for any kind of regular fork. But you can still hit them
with a 2-spin fork. The aiming point is a little
higher than a regular fork, because the shot must have
about twice as much airtime, although it will still be in the
air less than a second.
<p>
<img src="2spinfork.jpg">
<p>
