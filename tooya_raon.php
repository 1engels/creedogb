Note: this shot does work with Thor, but it can be dangerious, dual this shot with caution.<br>
<br>
Raon Drop (by Tooya)<br>
<br>
When you start off, most of the time you will not have a shot gun angle, so you can either use shot 1 to bury your enemy in a lil' hole... or you can preform this little trick. The idea is use shot 2 with your lowest possible angle, and nearly minimal power. A good power range would be under 1 bar. The more power you use the greater change the 2 Mines will seperate. If you can do this with a lightning or thor available, then by all means, dual. Thor or lightning guarantee your shot to explode on the enemy.<br>
