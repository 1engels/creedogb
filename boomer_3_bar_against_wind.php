Note from Creedo:<br>
If this is unclear, I'll try to explain it a bit.<br>
It helps if you already understand fixed power shooting and measuring.<br>
<p>
Step 1. Estimate how far away the enemy is from you in parts, or angles.<br>
1.0 screen distance = 50 parts.  <br>
1/2 screen distance = 25 parts.<br>
Every 10 parts is 1/5th of a screen.<br>
<p>
Let's say you know for sure that a half screen distance is from the left edge of<br>
your screen to the middle of the wind marker.  You have an enemy who is about<br>
1/5th of a screen beyond that. <br>
If half a screen is 25 parts, and the enemy is about 1/5th of a screen further than<br>
that... you're adding 25+10 parts to get 35 parts.<br>
Therefore the enemy is 35 parts/angles distance from you.<br>
<p>
Step 2. Starting at 90 degrees, lower your angle by the number of parts you<br>
estimated in step 1.  So if the enemy is 35 parts away... 90-35 = 55. Use angle 55.<br>
Because this is a 3 bar formula, your shot will be angle 55, 3 bars.  But your math<br>
is not finished yet... the strength and direction of the wind can really alter where<br>
your shot lands.  So you must do step 3 to adjust for the wind.<br>
<p>
Step 3. You're going to lower your angle based on the wind's strength and direction.<br>
To do that, take the wind's strength and multiply (or divide) it by whatever wind<br>
factor matches the wind's current direction.  To see what wind factors you need for<br>
the different wind directions, see this link below:<br>
<img src="hookwind.jpg"></img>
<p>
Let's say the wind is blowing diagonally up and against you, and wind strength is 12.<br>
The chart says to use wind/3. Dividing 9 by 3 gives us 4. Therefore you want to take <br>
your current angle and lower it by 4. If your original angle were 55, it's now 51.<br>
Rehashing our math: The enemy is 1/5th of a screen more than half a screen away from you.<br>
The wind strength is 12 pointing diagonal up/against. <br>
We therefore use angle 51, 3.0 bars for such a situation.<br>
I hope it was clear.<br>
<p>
Tip for experienced fixed-power users: If you already are familiar with measuring your<br>
screen by dividing it into 30 parts, then doing this formula with 50 parts isn't that<br>
difficult.  I'll call the 30 part method the cbchui system.  Every 3 angles under the <br>
cbchui system is 5 angles under the 50-part system.  So an enemy who is 21 parts away <br>
using your familiar cbchui measurement would be 35 parts away using this 3 bar hook.<br>
Just divide the cbchui measurement by 3, then multiply the result by 5 to get the new<br>
measurement. <br>
