OK NOW I WILL GIVE AN EXAMPLE SITUATION TO SHOW YOU HOW IT WORKS<br>
<br>
-the target is exactly one screen away on the right of you<br>
the wind is 18 directly pointing against you<br>
-so first u find out the power and angle for 0 wind, for this case lets use angle 60 with 2.7 power<br>
-to adjust angle - determine the chart to use, for this case we use high angle chart<br>
-the chart says -0.4, so we multiply 18 by 0.4, which gives 7.2, we round off to 7, so the angle to use is angle 53<br>
-to adjust power - determine the chart to use, for this case use high angle chart<br>
-the chart says 6W+0.1, so we increase 0.3 in power, because 18 wind = 3 X 6 wind, so the power to use is 3.0<br>
<br>
-therefore for this situation, u use angle 53 with 3.0 power to hit the target<br>
