<body><h1>BOOMER 2.8 FIXED SHOOTING</h1><br>
For 0 wind, wind against, and light wind towards.<br>
<br>
0 wind (and weak winds towards enemy):<br>
----------------------<br>
<br>
Step 1: Measure the distance to your enemy using high angle<br>
parts, like<br>
81 full = 1.0 screen distance = 9 parts.<br>
84 full = 2/3 screen distance = 6 parts.<br>
87 full = 1/3 screen distance = 3 parts,<br>
and so on.<br>
<br>
Step 2: Double the number of parts and subtract your<br>
answer from 90. <br>
So, if an enemy is 1 screen away that's 9 parts,<br>
double that and you have 18 parts.<br>
90 - 18 = 72.<br>
Or, an enemy is 1/3 screens away (3 parts).<br>
Double that and you have 6 parts.<br>
90 - 6 = 84.<br>
<br>
Step 3: Using the angle from step 2, fire with 2.8<br>
bars of power and you will hit.<br>
<br>
What's the advantage to using this when you already know how to hit<br>
with full power?<br>
<br>
1. Your screen is divided into 18 parts instead of just 9 big parts,<br>
there's no place an enemy can hide between 2 angles. For example<br>
using full power, there's no way to hit exactly half screen. Using<br>
the 2.8 method, you can choose angles in between your usual high<br>
angle spots (like to hit half screen you can use 81, 2.8 bars).<br>
<br>
2. Looks cool, not many other people know it or use it.<br>
<br>
3. Wind effect isn't as big when you use less power, making it<br>
easier to adjust for wind.<br>
<br>
4. You can add or reduce power from your miss, while a full power<br>
shot cannot have power added to it.<br>
<br>
Wind adjustment:<br>
0-5 wind: Raise your angle wind-1 angles. So in 4 wind, raise 3.<br>
6-9 wind: Raise your angle wind*1 angles. So in 9 wind, raise 9.<br>
10-13 wind: Raise your angle wind+1 angles. In 10 wind raise 11.<br>
Over 13 wind is up to you, you might want to use a different shot.<br>
<br>
Wind against you<br>
----------------<br>
<br>
1. You can imagine the screen being divided into 60 parts, or if<br>
you use the familiar hook and cbchui 30-part measurements, imagine<br>
that each screen part is doubled. Measure the distance to your enemy<br>
based on 60 parts (or 30 parts * 2).<br>
<br>
1/4 screen = 15 parts.<br>
1/3 screen = 20 parts.<br>
1/2 screen = 30 parts.<br>
2/3 screen = 40 parts.<br>
3/4 screen = 45 parts.<br>
1.0 screen = 60 parts.<br>
<br>
Subtract the number of parts (based on enemy distance) from 90.<br>
<br>
So an enemy 2/3 screen away is 40 parts, 90-40 = 50.<br>
<br>
2. Adjust for wind by lowering your angle.<br>
Use this windchart - ignore the text at the top, you're not using 75/60 as half<br>
and full screen now... you're using 60 and 30:<br>
<br>
<img src="hookwind.jpg"><br>
<br>
So for example, in 10 wind pointing down and back, lower 7 angles.<br>
The +1 to +5 means add 1 to 5 angles depending on how strong the<br>
upwards wind is.<br>
<br>
3. Shoot with 2.8 bars of power.<br>
<br>
The advantage to this: There isn't much advantage but it's cool to<br>
know and very accurate (60 part screen). It's good for near 1 screen<br>
distance. Too much closer and your angle will be near 60 and risk<br>
a backshot. Too much further and 2.8 bars isn't enough power to reach.<br>
If the formula tells you to use an angle below 20 or so, you'd better<br>
do a bit more than 2.8 bars because the wind is so strong.<br>
<br>
If you have trouble calculating this but calculate hook shots well,<br>
you are simply lowering your angle by twice as much as you would<br>
for making a hook shot (BEFORE wind adjustment). So if you calculate<br>
hooks properly (30 parts to 1 screen) you only need to count the<br>
number of parts between you and the enemy, double the answer, and<br>
subtract from 90... then reduce (or increase) your angle further<br>
based on Vexez' hook wind chart. Remember angles near 60 risk a<br>
backshot, this formula isn't really for 1/2 screen shots, it's for<br>
long shots - 3/4 screen or more.<br><br>
