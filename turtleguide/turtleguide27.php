<p>
Some fork advice: Hitting on your first try is difficult. But you should almost always 
be able to hit on your 2nd try if you can understand how your first shot failed.
If your first shot misses because the shot didn't have enough time 
to come together:
<ul>
<li>If the angle is over 45, then add a little power, and use a higher angle if the 
shot travelled the correct distance. Usually you only need to add 2 or 3 angles 
and .1 bars of power or so.
<li> If the angle is under 45, then you must again use a higher angle, but this 
time you reduce power. I'll explain why in the next section. Again no more than
2 or 3 angles, and .1 bars of power. 
</ul>
If your shot misses because it's in the air too long (fifth 'crossing' happens
somewhere in front of enemy):
<ul>
<li>If the angle is over 45, reduce power a bit (.1 or less) and lower 2-3 angles.
<li>If the angle is under 45, lower 2-3 angles and add some power. In some cases,
just adding power is enough.
</ul>
If the shot's 'spin' is correct but crosses somewhere behind them, and your 
shooting angle is over 45, then simply raise angle. You may also wan to use
1 or 2 pixels less power, but using the exact same power should be ok.
If the shot's spin is correct but lands in front of them, you need to 
lower your angle and then use the same power (or 1 or 2 pixels more).
For angles under 45, if the shot flies over their head, but crosses at
the correct place, simply lower your angle and don't touch the power.
If the shot hits the ground below them, then raise angle and use the same power.
<p>
Remember that 1 angle change when forking means almost nothing. If you're 
going to fix your fork, you need to adjust at least 2, and often 3 angles.
Also remember that the adjustment gets larger when you're dealing with wind, 
especially opposite or down wind. Angle adjustment also gets larger when 
dealing with elevation and wind. For example in opposite wind, I might 
adjust my angle twice as much for elevation as usual. This is a matter of feel 
unless you want to take the time to grind out the numbers and make 
a table.
<p>
Lastly, some people calculate forks a different way, and you may find you 
like this way better. If you want to try it, here's 
<a href="../remarkfork.htm">Remark's fork info</a> 
for up to half screen.
