3 Bar Power Method<br>
<br>
With This method you use a constant power of 3 bars and change angle. The screen has 20 parts, which is actually easy to remember. Half screen is angle 80, full screen is 70. Therefore, since 1/4 is a half of 1/2, 1/4 is angle 85, 3/4 is angle 75. With this method you can also consistently shoot over 1 screen distance. 1&1/4 is angle 65, 1&1/2 is angle 60. You can keep going after this, but a) You'll never need to and b) the angles get smaller so it isnt so consistent anymore.<br>
Recap:<br>
1/4 - 85<br>
1/2 - 80<br>
3/4 - 75<br>
Full - 70<br>
1&1/4 - 65<br>
1&1/2 - 60<br>
<br>
This is my preffered method of shooting. It has nice easy-to-remember angles, a consistent power (It really doesnt change :o!), and is pretty accurate. There are some blindspots, so you will either need to move the turtle a little bit or adjust power, but you usually won't need to. There are disandvantages, however. You may find it hard to get an angle sometimes. During these situations you may find it helpfull to use 2.4 (UGH) method.<br>
