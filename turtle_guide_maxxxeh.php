Timebomb<br>
<br>
Timebomb is the way most people see the SS happening. You float the little blob to them, then just before it hits them, it explodes in their face. You will generally use 2 bars power, sometimes more, sometimes less. 90 1.8 hits yourself. Angle 67 2 bars is halfscreen. 79 1.85 (a little more than a 90 SS, otherwise not enough air) is 1/4. 3/4 is 53 2.15 bars, and full is 48 2.3 As you can see, you dance around 2 bars power.<br>
Recap:<br>
1/4 - 79 - 1.8<br>
1/3 - 75 - 1.85 (1/3 is from "all" button to half power)<br>
1/2 - 67 - 2 bars<br>
2/3 - 60 - 2.1 (2/3 is from left edge to the Sudden Death / Item box)<br>
3/4 - 53 - 2.15<br>
Full - 48 - 2.3 (some say 49, some say 47. I happen to like 48)<br>
<br>
Timebomb usually gets a lot more Ooooh's and Aaaah's, but, likewise, is also screwed up a lot easier. You will, however, usually have a better angle for this. The enemy will not always be level with you and a good distance away. As they get farther away, lower angle + raise power a little bit. As they get higher in elevation, raise power, lower, drop power.<br>
