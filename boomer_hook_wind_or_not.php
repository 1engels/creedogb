<body>To determine if the wind direction allows for hook shots <br>
or not, use this guide.  First look at wind strength.  Then look<br>
at the wind's angle.  Note by 'angle' I don't mean boomer's angle,<br>
I mean the angle of the little red arrow that your wind indicator<br>
uses to show wind direction.  <br>
90 = wind straight up or down<br>
45 = wind diagonal up/against or down/against.<br>
0  = wind pointing perfectly left or right.<br>
One trick you can use to judge the wind direction if you have<br>
some time between turns: Move boomer's angle to the angle<br>
indicated by the chart.  If boomer's arrow is pointing at an <br>
angle that appears to be more horizontal than the wind's direction<br>
then you will get a hook.  If the angle appears more vertical than<br>
what you're seeing on the wind indicator, it won't hook.<br>
