<h1>6. Hooks</h1>
<hr>
<blockquote>
Hooks are another basic boomer skill, because they allow you to make shots 
against the wind based on a formula.  A normal shot against the wind is very tricky
if all you have to go by is 'feel'. Hooks are very simple though, power is always
full so you only need to change your angle to hit different places on the screen.
The distance between angles is so small you never will need to change power,
just find the right angle.
<p>
To find that angle, first measure the distance to your enemy using a 30 part
measurement system (it's not as hard as it sounds). That will give you a
'base angle'. Then lower your angle based on the wind's strength and
direction to get a 'final angle'.<br>
You can use Vexez' hook windchart to figure out how many
angles you need to lower in order to adjust for the wind. Then
just fire with full power.
<p>
<h2>Hook Formula</h2>
<p>
Hooks are based on 30 angles (or parts) per 1 screen.
But can anyone really imagine 30 little parts on their screen?
Of course not. So what I do is memorize where every 5th part will be.
That means that I'm really dividing the screen into 6 parts,
and each part has 5 angles in it:<br>
<ul>
<li>90-85
<li>85-80
<li>80-75
<li>75-70
<li>70-65
<li>65-60
</ul>
<br>
If it's easier for you, memorize these landmarks and use
the image below to spot them easily.
<ul>
<li>80 = 1/3 screen
<li>75 = 1/2 screen
<li>70 = 2/3 screen
<li>60 = 1.0 screen
</ul>
<p>
<img src="hookgrid.jpg"></img>
<p>
So that image teaches you your BASE angle - but it doesn't show you
the FINAL angle. You must now calculate wind adjustment. Even 2 wind
requires an adjustment. So use this windchart to figure out how many
angles to lower your shot. In some rare cases, with very vertical hook
wind, you will raise your angle, but usually you will be lowering it.
<p>
<img src="hookwind.jpg"></img>
<p>
<b>Other hook tips</b>
<ul>
<li>The more strong the wind is blowing against you, the more you must
lower your angle to hit the same place on the map.  
There's also a greater chance that the hook shot will blow backwards.  Wind 
below 6 is generally not going to blow back except maybe for very close 
shots.  For wind at 8 or more, there's a chance a shot will blow backwards 
unless you fire at a low enough angle (i.e. you're shooting at a target 1 
screen away).  Angle 89 never flies back.

<li>Wind direction matters a LOT and you must decide:<br>
<b>1.</b> IF the shot will hook<br>(like in wind 2 pointing at 11:00 o'clock, is it enough to hook or not?)<br>
<b>2.</b> IF the shot will fly backwards<br>(will a half screen hookshot in 14 wind blow back?)<br>
<b>3.</b> How far your hook will travel at any given angle<br>
<p>
<b>About 1:</b> The more perfectly left or right the wind is, the more likely it is 
that your shot will hook.  Unfortunately, that also means it's more likely that
your shot will blow backwards. For example: 2 wind pointing directly left/right will hook.
Two wind a little left/right, but mostly up or mostly down ...will not hook.  You 
will need to use your best judgment for wind directions that are a tiny bit 
against but mostly up or down... you might find that at certain wind directions, 
the wind might hook at, e.g. 17 strength, but not at 16. A pretty good rule of 
thumb is that wind 4 against will hook in most wind directions.
A way to judge whether the shot hooks or not based on other boomer's shots:
If a boomer (not you) shoots and you see the shot bend/dip sharply as it 
travels, then it is 'hook wind'.  If a boomer does a backshot, then it is also 
'hook wind'.  If you see another boomer do a hook then... durrr.
<p>                                                                                           
If you have a lot of trouble figuring out if wind will hook or not, check out
<a href="hookornot.php" name=10a>this section</a>, which shows visually how to judge it.
<p>
<b>About 2:</b> This is tricky, just remember the further your target is, the less 
chance you'll get the shot to fly backwards.  Also, the more up or down the wind
is, the less chance there is the shot will fly backwards... for example even 20 
wind won't make the shot fly backwards if the wind is mostly up or down and 
barely against.  Experience is what counts here.  Try a hook and if it blows 
back, forget hooking until the next wind change, or aim for a faraway target.
Also be aware some angles are just buggy and want to fly back a lot, for 
example 87 and 84 seem to fly back when they shouldn't. I worry most about
blowback when shooting closer than half screen, and when wind is stronger
than 12 or so.
<p>
<b>About 3:</b> The more downward the wind points, the closer the shot will land to 
you.  For example, if a 1 screen shot in low wind is 57, a 1 screen shot with
strong downward wind might be 50.  Similarly, upward wind makes the shot drift
quite a ways after it flips over, so a 1 screen shot with strong upward wind 
might be 64. That's why it's important to learn the chart and not just memorize
a few angles.
<p>
<li>Note that in all cases you are lowering your angle except for wind that is 
blowing mostly upwards.  In that sort of wind you'll need to raise your angle.
I sort of go by this loose rule for hook wind pointing almost straight up:
Raise your angle by  (wind/5) angles.
<p>
There is also a method for adjusting your hookshot to compensate for a tornado.
I credit Rearview for this info.
<p>
-If the tornado is fairly near the enemy, like ~2 mobile widths: 
Find the angle needed to hit normally, then lower your angle by 4 degrees.<br>
-If the tornado is 3-4 mobile widths from the tornado, lower your angle by 3.<br>
-If the tornado is 4 or more mobile widths away from the nado, lower by 2.<br>
Remember that the hook shot must finish bending before the tornado.
<p>
Some other information from Petroleum:<br>
1). If hitting tornado at a high point, raise angle by 1 and use 0.3 less power. <br>
2). If hitting tornado at a low point, just lower your power by 0.1.<br>
