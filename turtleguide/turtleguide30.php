<h1>Last advice and credits</h1>
<hr>
<blockquote>
<ul>
<li>If you want to win, playing smart is better than shooting accuracy.
<li>Keep playing even when the game seems unwinnable, or even if you 
had several bad games in a row. Don't be one of those jerks who quits 
because he missed a dual. Don't leave because your teammate lagged out. 
Many games are winnable even when 1 player misses every shot, and a lagged 
out teammate is pretty much the same as a teammate who keeps missing. 
In desperate situations, use every small advantage you can... ruin their angle,
force them to move, move yourself and force them to adjust their shot, get a delay 
advantage, watch for weather changes, etc. If you lose anyway, who cares. 
If you're into Gunbound and serious about it, you'll play 10,000+ games. One 
loss is meaningless. 10 losses are meaningless. Don't quit over small things.
<li>The best players I've seen stick to one bot for a very long time. If you think 
Turtle is "your" bot, then don't switch just to end a losing streak or because you 
think the enemy's bot has an advantage over Turtle. Keep playing until 
you win or stop caring.
<li>Try learning other formulas, if you're not sick of them already. Sometimes 
it helps you understand Turtle better. For example you could learn Armor's 
fixed 35 formula to get better at low angle Turtle shots.
<li>Learn from every shot, especially misses.
<li>When my mood starts to suck and my playing goes downhill, I try to calculate 
my shots harder. I don't know if it'll work for you, but it helps me. The nice thing about 
formulas is that they don't care about your mood, the numbers don't change.
<li>If you keep losing despite your best efforts and my best advice, try another room. 
Some people feel ashamed to ask good players to leave, or ask their friends to not 
play against them. I don't. If I'm losing over and over, whether it's to superior skill or 
bad luck, then I will change rooms or ask my opponents to go. Of course, some of 
you may not care about winning at all (I don't believe it but...). But even players 
like this might find that losing 5 or 10 times in a row ruins their mood. There's no 
point in playing a video game if it's not fun, so if you're not having fun... 
do something about it.
</ul>

