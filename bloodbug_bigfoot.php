1. Shots/Mobile<br>
<br>
Shot 1(Missiles, Bombs)<br>
Damage : 180-200<br>
Angle : 20, 10 True Angle<br>
Delay : 780<br>
Projectiles : 4<br>
<br>
The shot you probably will use most of the game. It does good damage, so you can probably outdelay people with bots like Boomer while dealing only ~50 damage less than them. It's also useful for digging holes, and then more holes. And tunnels. Yeah. To bunge, just aim a little foward of them. They may or may not stay on a little ledge with the hole beneath them depending of how you aimed, so don't blame me.<br>
---<br>
<br>
Shot 2(Paintbucket Shotgun, Beercan Shotgun)<br>
Damage : 150-310<br>
Angle : 20, 10 True Angle<br>
Delay : 780<br>
Projectiles : 6<br>
<br>
The other shot you will probably use. This one used to be a good bunger, but Softnyx nerfed it so it falls lined, which isn't good if you want to bunge something. Why the wild damage differences? Why can't you hit 310 even if you hit all the 6 beercans in? I'll explain all of that in section 2.<br>
