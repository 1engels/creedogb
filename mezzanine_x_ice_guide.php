There are 2 ways of playing ice. Dealing damage, and Taking it.<br>
Dealing damage is a good strategy because so many games are dual and dual+ only.<br>
<br>
You want to start our your turn powerfully. If you are sure of a shot, dual+ first turn. If you are not so sure, use the low delay shot 1 as a test. If you do 2 dual+ in a row, they should be significantly damaged. If their health is at about 40-45% of the life bar, you should be then able to kill them with a dual. If you are out of dual+ and they have about 1/2 life, use shot 2 on them before the final dual.<br>
<br>
How to Shoot Dual+<br>
Because shot 1 bunges a little, you should hit either their front, or the land right at their feet. If you hit the middle of them, it will lower them and shot 2 will fly slightly over their head. Because of the shot 1 bunge, this gives the illusion of the shots flying distances, but I am sure that they go exactly the same distance.<br>
<br>
Shots will do more damage over time. A dual+ does more damage the second time. The final dual will do about 400 damage if you dual+ twice before, and hit them with one more shot 2. In score, you can get their defense much lower. At the end of score games in double death, you will be doing around 500 with your shot 2. If you somehow get their defense to -50, by slightly missing, you will do insane damage.<br>
