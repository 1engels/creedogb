<h2>Angle 42 bjsl formula</h2><br>
<b>(Made by me, inspired by HongHong bjsl 50 and Sayo 40 formulas):</b>
<hr>
<blockquote>
This formula is a very good method for making shots against the wind with 
boomer.  It is an alternative to hook shots and may help in situations where a 
hook will fly back.  It's also good for showing off and making nice SS shots.
<p>
<b>Formula:</b>
<br>
The full formula would be
<b>42 - (WindStrength*WindFactor)</b>
<p>
Power is judged by user using fixed power points described below.
The power levels can be memorized using this trick:<br>
half: 2<br>
3/4: 2.5<br>
full: 3<br>
Those are easy to remember, right?
But they're not accurate. To be accurate just add .1 to
the half and 3/4 distance powers. These
numbers are to make the formula easy to learn,
and with time you'll figure out the exact powers, which
I also show below. Keep in mind that in strong wind
(like over 15 or so) you need to add a bit of power.
<p>
<table>
 <tr>
  <td>Exact power points</td>
 </tr>
 <tr>
  <td>1/4 screen</td>
  <td>1.45 bars</td>
 </tr>
 <tr>
  <td>1/2 screen</td>
  <td>2.1 bars</td>
</tr>
 <tr>
  <td>3/4 screen</td>
  <td> 2.6 bars</td>
</tr>
 <tr>
  <td>1.0 screen</d>
  <td>3.0 bars</td>
 </tr>
</table>
<p>

If the enemy is between these set points you must guess beforehand how much 
power to alter your shot, for example between 1/2 screen and 3/4 screen I might 
use 2.35 bars or so.
<p>
Once you pick your power, you now choose your angle.
Start at angle 42, then calculate a new angle using a method 
similar to calculating hooks and backshots.
Multiply Wind Strength * Wind Factor (see the chart below).  Then subtract the answer from angle 42.
Use the new angle with the same power you already chose before.
<p>
The wind factors are either very similar to or the same as your hook wind 
factors.
<p>
<img src="bjsl42wind.jpg"></img>
<br>
<h3>Some examples</h3><br>
<br>
You are on left, enemy is on right.
<hr>
Wind is at 10:30 against you. Wind strength is 12. This wind factor shows "wind/3"
So take 12, divide it by 3. The answer is 4. Starting always at angle 42, you
now will lower 4 angles.
The enemy distance is half screen, so we'll use 2.1 bars of power with
our new angle 38.
<hr>
Wind is 15 directly against you (9:00 o' clock). Wind strength is 18. This
wind factor shows "80% of wind" so we will multiply 18 by 80%. The answer
is around 14, so starting at 42 we lower 14 angles to 28.
Enemy is 1 screen distance, so usually we would use 3 bars, but remember
that for strong winds it's best to add a bit of power. So in this case I'd add
about .15 bars, so final angle and power is 28, 3.15.
